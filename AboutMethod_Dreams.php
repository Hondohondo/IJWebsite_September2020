                  <h1>Dr. Progoff Discusses: Working with Dreams and Imagery </h1>
                  <h6>Using Dreams and Imagery to Develop Your Life</h6>
                  <p>
                    The nature of dreams and images is an expression of what is in the depths of us at the deeper-than-conscious level.
                    They provide material for the contents of our lives, carrying our seed potential of what our lives can become, and a certain wisdom and guidance for the way this process can take place.
                     (Whatever I say about dreams applies equally to Twilight Imaging.)
                  </p>

                  <p>
                   We want to have access to that material, not so much to find reasons for our condition, but rather to draw forth leads for the next development that can take place in our lives.
                   We want to approach our dreams with the question: “to what aspect of my life does it lead me?”
                  </p>


                  <p>
                    I feel it is important to emphasize that no one dream or image, taken by itself, is necessarily relevant because it may be a phase of a cycle in our lives.
                     It is preferable to work with a cluster of dreams and get a feeling for them.
                     I would rather use the word “cluster” than “series,” because they are not necessarily consecutive in time.
                  </p>

                  <p>
                  I urge you to refrain from interpreting or analyzing the dream, even if the line of meaning seems self-evident to you.
                  Also, I think that one of the problems in our habit of thinking about insights or analysis is that they become a place for stopping.
                  You may say “Oh, I see what it is,” and the process of movement and development ends. Rather, we must let the dream process continue in our lives.
                  </p>

                  <h6>Developing Our Dreams </h6>

                  <p>
                    We have a number of procedures and places in the <em>Intensive Journal</em> workbook to open out and develop our dreams.
                     The dream content may be readily apparent and can be taken at face value.
                     In working with a dream about a person, that person may remind you of some event which could be described as a memory in the Life History Log section.
                     You may enter into a dialogue in the Dialogue with Persons section where the relationship is immediately significant. The dream may lead you to a variety of other sections.
                  </p>

                  <p>
                    On the other hand, the symbolism may be obscure.
                     We have two principal ways to develop our dreams further.
                   </p>

                   <ul>
                     <li><h6> Reconstructing and Extending the Dreaming Process </h6></li>
                     <p>
                      First, we may go back to the twilight and let the process of dreaming pick up and continue.
                       After all, any dream that we remember is just an excerpt of the larger dreaming process because we did not remember it all.
                        We can reconstruct the way the dreams and images have been taking place, moving back in time so that we can get a sense on a deeper than conscious level of the way the future is trying to unfold.
                        We feel our dreams and images and reconstruct them in the Dream Enlargement and Imagery Extension sections.
                     </p>
                     <p>
                    To reconstruct does not mean to interpret, but rather to put ourselves back into the process of dreaming (or imaging), and let ourselves feel it and let it continue itself.
                    We are picking up those images and letting them extend. We call this process Twilight Dreaming.
                     </p>

                     <p>
                      A major reason why I prefer to speak of the twilight state rather than the dream state is that at the twilight level, we can remember more and we can move around more.
                       Because we are awake at the same time that a non-conscious process is moving in us, twilight imaging is very often the most fertile place to work, and is probably, in the long run, more fruitful for us than relying on dreams.
                        We have access to material from the deeper than conscious level even if we do not dream or remember our dreams.
                     </p>

                     <li><h6>Correlating to Areas of Our Lives</h6></li>
                       <p>
                         Second, we may see the correlation or parallel between the process that is in the dream and what is taking place in our lives.
                          Our lives move outward and forward until something happens that stops or blocks the process.
                         Then, they move in and backward and down into the deeper than conscious level, back in time to some part of our past experience that may be relevant to the issues at present.
                       </p>
                       <p>
                         As we read back a cluster of dreams, feeling their tone, quality, atmosphere and movement, we ask: “to what aspect or time in my life do these dreams seem to correspond?” The dreams may lead to more than one possible area, and you may work through each one of them.
                          We work from the perspective that we do not need final answers or endings.
                         We are really looking for leads that will progressively open up areas of our lives.
                       </p>

                   </ul>


