                  <h1>Whole-Life Process: Greater Perspective and Safety</h1>

                  <p>
                    Progoff’s “whole-life" approach provides important benefits of perspective and safety.
It also allows for the possibility of the <a href="AboutMethod_JournalFeedback.php" class="page-link no-margin">Journal Feedback process</a> to occur, consisting of working from one exercise to another to create new integrations of awareness which Dr. Progoff called the “essence of the method.”
                  </p>

                  <h6>Develop a Solid Foundation for Decision Making</h6>

                  <p>
                    The whole-life process refers to working in broad range of areas of your life such as personal relationships, body and health, career and special interests, life history, dreams and imagery, and meaning in life.  The <em>Intensive Journal</em> workbook is a structured instrument that has exercises in each of these areas so that you can work in each area in an organized and systematic way.  Greater awareness about a wide range of life experiences creates a solid foundation for making decisions.
                  </p>

                  <h6>View Issues in the Context of Your Life to Make More Optimal Decisions </h6>

                  <p>
                    Issues can be viewed in a larger context and become more manageable.  By accessing more areas of our life, we are better able to realize we have more resources and capabilities within ourselves to deal with the issues we are facing.
                  </p>

                  <p>
                    Since we are working with our entire life, we are more likely to be in touch with ourselves, and therefore, more likely to make better decisions.   We are placing ourselves within the moving context of our life between the past and future potentials which Dr Progoff called “Life Positioning.”
                  </p>

                  <h6>
                    Consider Issues From Different Vantage Points
                  </h6>

                  <p>
                    Sometimes, it is helpful to view an issue in a different way to gain another perspective.
Viewing an issue indirectly can create an awareness where perhaps assessing it directly would not be productive.
                  </p>

                  <p>
                    Since the <em>Intensive Journal</em> workbook uses the whole-life approach with its broad range of exercises, we can view issues different ways.  For example, using the dialogue technique, we  may begin with a dialogue with our spouse in the Dialogue with Persons exercise.  Then, to gain a different perspective, we can engage in a dialogue with the marriage itself which has a life of its own (Dialogue with Situations exercise).
                  </p>

                  <h6>
                    Obtain Greater Balance and Safety
                  </h6>

                  <p>
                    The whole life approach provides greater balance and therefore safety, especially when  working with the deeper-than-conscious.  Dr. Progoff said:
                  </p>

                  <blockquote class="u-margin-bottom-small">
                    “The very power that is contained in the depth of the human psyche can lead to symbolic overbalancing and can cause the individual to topple over in the world.....[With the broad structure of the <em>Intensive Journal</em> method], imagery experiences are set within the context of the movement and contents of the life as a whole.” <sup>1</sup>
                    <!-- <p class="right-span"></p> -->
                  </blockquote>

                  <p>
                    By working with our entire life, we are receptive to imagery, intuitions, and experiences from all areas that provide the opportunity for new awareness.  We are not shutting out possibilities in our lives that could provide a rich source of perspective and insight.
                  </p>

                  <h6>
                    Guard Against Premature Judgments and Decisions
                  </h6>

                  <p>
                    Using ProgoffTM  techniques allows for issues to develop, protecting you from premature judgments and decisions.   By working with our entire life, we can be patient, working in one area and then moving to other areas.  We are not dependent upon a certain aspect of our life for awareness, but rather remain patient and receptive for what can unfold from all possible areas that comprise our rich life.
                  </p>

                  <h6>Take Advantage of the Journal Feedback Process </h6>

                  <p>
                  The whole-life approach also provides the structure and opportunity for the Journal Feedback process to work.   Through the <a href="AboutMethod_JournalFeedback.php" class="page-link no-margin">Journal Feedback process</a>, we are moving back and forth between different areas of our life, reading and writing and then moving to related sections.  Having the broad foundation of our life through the whole-life approach in the form of the <em>Intensive Journal</em> workbook allows for this process to unfold.
                  </p>


