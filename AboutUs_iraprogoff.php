                <p>
                  Dr. Progoff completed his doctoral dissertation on the psychology of C.G. Jung from the New School for Social Research in New York City.
                  His thesis was published in 1953 as Jung's Psychology and Its Social Meaning.
                  After reading Dr. Progoff's dissertation, Dr. Jung invited him to study with him in Switzerland as a Bollingen fellow. He studied with Dr. Jung in 1952, 1953 and 1955.
                   In addition, Dr. Progoff gave several Eranos lectures in Switzerland during the 1960s, where he presented his theories of holistic depth psychology.
                </p>

                <p>
                  As a practicing depth psychologist and Director of the Institute for Research in Depth Psychology at Drew University from 1959 to 1971, Dr. Progoff conducted research on the dynamic process by which individuals develop more fulfilling lives.
                   As a psychotherapist, he found that the clients who wrote in some form of a journal were able to work through issues more rapidly.
                    Through this research, he then developed and refined the <em>Intensive Journal</em> Method in the mid-1960's and 1970's to provide a way to mirror the processes by which people become dynamic and develop themselves.
                </p>

                <p>
                  Dr. Progoff conducted numerous <em>Intensive Journal</em> workshops throughout the United States and Canada.
                  Through this process, he was able to develop the Method further.
                  In 1975, he completed the award-winning book, At a Journal Workshop, which articulates both his theory of personal and spiritual growth and how to use the <em>Intensive Journal</em> Method.
                   In 1980, Dr. Progoff completed The Practice of Process Meditation which set forth the principles and exercises for the Meaning (Process MeditationTM) Dimension of the <em>Intensive Journal</em> workbook.
                   (In 1992, Dr. Progoff issued a revised edition of At a Journal Workshop which combines in one volume the original edition and The Practice of Process Meditation.)
                </p>

                <p>
                  As interest continued to grow in the 1970's, Dr. Progoff formed the Progoff (National) <em>Intensive Journal</em> Program to make workshops available to the public through its trained and certified leaders.
                   Over 250 workshops are conducted annually.
                </p>

                <p>
                  To further his goal of providing a tool for self-development, Dr. Progoff has made workshops available at prisons, social service agencies and health care facilities.
                  It is part of his commitment and dedication to providing a tool for individuals of limited economic means to develop themselves.
                </p>

                <p>
                  Regrettably, Dr. Progoff passed away in January of 1998 after suffering from an advanced form of a rare neurological illness, Progressive Supranuclear Palsy (PSP).
                  This disease affects the central nervous system and greatly weakens an individual's body.
                  Dialogue House is committed to carrying on the work of Dr. Ira Progoff based on his research and writings.
                </p>

