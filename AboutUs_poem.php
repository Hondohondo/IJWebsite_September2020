                <h1>WISDOM OF LIFE</h1>
                <p><em>by Ira Progoff, PhD</em></p>


<div class="poem">

  <blockquote class="poem__quote u-center-left">
  If I did not believe <br class="u-margin-bottom-small-1">

That the wisdom of life   <br class="u-margin-bottom-small-1">

Is greater than my own wisdom, <br class="u-margin-bottom-small-1">

I could not have survived, <br class="u-margin-bottom-small-1">

But having survived, <br class="u-margin-bottom-small-1">

It is more than a faith now,  <br class="u-margin-bottom-small-1">

A knowledge.  <br class="u-margin-bottom-small-1">

I know,  <br class="u-margin-bottom-small-1">

That, great as my wisdom is,  <br class="u-margin-bottom-small-1">

--Almost as great as my will and my desire--  <br class="u-margin-bottom-small-1">

Yet the wisdom of life is greater.  <br class="u-margin-bottom-small-1">

And, as I could not float upon water when I tried to,  <br class="u-margin-bottom-small-1">

Now I can float upon life  <br class="u-margin-bottom-small-1">

Without trying.  <br class="u-margin-bottom-small-1">

In this is my wisdom  <br class="u-margin-bottom-small-1">

And the wisdom of anyone  <br class="u-margin-bottom-small-1">

To know that I know not  <br class="u-margin-bottom-small-1">

How to carry the weight of my existence.  <br class="u-margin-bottom-small-1">

But the waters of life will carry it for me  <br class="u-margin-bottom-small-1">

In their wisdom.  <br class="u-margin-bottom-small-1">

That is the wisdom of life  <br class="u-margin-bottom-small-1">

From which comes all power  <br class="u-margin-bottom-small-1">

And the ultimate glory.  <br class="u-margin-bottom-small-1">

And the greatness of my wisdom lies  <br class="u-margin-bottom-small-1">

In letting life be wise.  <br class="u-margin-bottom-medium">

Written by Dr. Progoff in 1972.  <br class="u-margin-bottom-small-1">
  </blockquote>


