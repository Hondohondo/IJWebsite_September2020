                <div class="quotes">
                  <blockquote>
           "One of the most significant psychospiritual books of the twentieth century."
              </blockquote>
                  <p class="right-span">Common Boundary Magazine Award</p>
                    </div>

                <p>
              Common Boundary’s distinguished committee selected 65 books of the twentieth century which transformed individuals or impacted the fields of psychology and the nature of spirituality, or both.
                </p>

                <p>
              This list included leading figures from the fields of psychology, philosophy and theology such as Sigmund Freud, Carl Jung, Joseph Campbell, Martin Buber, Norman Vincent Peale, Carl Rogers, and Rollo May.
                </p>

                <p>
            Dr. Ira Progoff was selected for his pioneering book on the <em>Intensive Journal</em> method - At a Journal Workshop (original edition 1975).
                </p>

