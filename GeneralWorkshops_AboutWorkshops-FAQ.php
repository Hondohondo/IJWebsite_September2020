                                  What happens at a workshop?
                                       </h6>
                                     </label>
                                     <!-- <input type="checkbox" name="one" id="question8"> -->
                  <div class="hide">
                    <div class="innerpara">
                                    <p>
                                  At your first workshop you receive your <em>Intensive Journal</em> workbook.
                                  Not all of the sections will be used in the Life Context and Life Experience workshops.
                                  These will be used in later modules of the <em>Intensive Journal</em> series.
                                    </p>
                                    <p>
                                  The workshop leader will guide participants through a series of journal sections based upon the structure developed by Dr. Ira Progoff in At a Journal Workshop.
                                   This structure is set up so that the sections complement each other and a continuous flow among them is achieved.
                                   The leader will explain each section and then allow time for participants to write in their workbooks.
                                    </p>
                                    <p>
                                Throughout the workshop the leader will allow time for questions about either the method or about the direction of a participant's writing.
                                 This may be done one-on-one in order to protect the person's privacy. The leader will also allow time for participants to read their work aloud.
                                 This is not meant to foster discussion about a person's writing, but is for the purpose of connecting the reader to and evoking his or her emotions.
                                    </p>

                                    <h6>Try a Sample <em>Intensive Journal</em>® Exercise</h6>
                                    <p>
                                      Now you can begin to experience how the <em>Intensive Journal</em> method works through a sample exercise.
                                      It is important to do the exercise in a setting of complete silence, with pen and paper (not computer), when you can relax and take your mind off your daily life.
                                      Record what comes to you; do not edit or censor. Write the date at the top of the page and the name of the exercise, "Period Log."
                                      You should allow about 45 minutes to complete the exercise.
                                    </p>

                                    <ol class="faq">
                                      <li>
                                        Describe the "Now" period of your life; an open ended period that has a beginning but no ending.
                                        Examples include: three years since you moved to a new city or started a new job.
                                      </li>
                                      <li>
                                        Record your thoughts, feelings, memories - whatever presents itself to you.
                                         Complete the phrase: "It is a time when...."; record images that describe the period.
                                      </li>
                                      <li>
                                        Describe more details about this period: people, projects or activities; your health; attitudes about society;
                                        important events; dreams or imagery; people who inspired you; and choices or decisions you made.
                                      </li>
                                      <li>
                                        Read back what you have written and record any thoughts and feelings
                                         that you have during this process of writing and reading back.
                                      </li>
                                      Through the Period Log exercise, you are beginning the process of reconnecting with different
                                      aspects of your life from which new perspectives and opportunities can reveal themselves.
                                    </ol>
