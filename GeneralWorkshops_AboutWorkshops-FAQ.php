<div class="hidepara">
                    <label for="question3">
                      <h6>
                  Can I bring a laptop computer to the workshop?
                     </h6>
                   </label>
                   <!-- <input type="checkbox" name="one" id="question3"> -->
<div class="hide">
  <div class="innerpara">
                  <p>
                   No. We believe that there is a therapeutic benefit from the physical process of recording your feelings and experiences.
                  </p>
                  <p>
                 The atmosphere in which you work in the <em>Intensive Journal</em> method is critical.
                 Using a computer tends to have the effect of taking you out of the deepening atmosphere and your inner process.
                  You tend to be in a more analytical mode, which is counter to a key principle for using the <em>Intensive Journal</em> method.
                  </p>
                  <p>
               You may feel that you could write a good deal more on a computer.
                We are interested in the quality of your experience and not the quantity of material that is written.
                  </p>
                  <p>
               Some people say that they could use a computer to conduct searches for relevant terms that they may have written.
               However, the Journal Feedback process of reading back and recording would be significantly affected and distorted by using a computer in this manner.
                  </p>
                  <p>
               Using a computer in a workshop can distract other participants and disrupt the quiet atmosphere.
                The noise of even a quiet keyboard can break the sensitive atmosphere that has developed in the workshop.
                  </p>
                  <p>
               The only exception to the rule of not allowing computers in the workshop is in cases where a person is physically challenged and would otherwise be unable to participate.
                In these instances, we recommend that the participant contact the hosting organization in advance so that suitable arrangements can be made.
                  </p>
