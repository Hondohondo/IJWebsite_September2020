                  <h1>Workshop Atmosphere</h1>
                  <!-- <h6>A Different Form of Meditation</h6> -->
                  <p>
                    The atmosphere in which the <em>Intensive Journal</em> method is used is unique and critical for the method’s effectiveness.
                     Imagine yourself in a room, where our leader guides you step-by-step through the <em>Intensive Journal</em> exercises.
                     The leader will describe an exercise, providing background information about its purpose and then outlining how the exercise can be done by you as you apply it to your life.
                  </p>
                  <p>
                  A period of quiet follows when you apply the exercise to your life.
                  Participants may ask questions and read what they have written voluntarily (see below).
                  We do not engage in intellectual discussion about our lives or the method in order to keep a deepening atmosphere of quiet.
                  </p>
                  <p>
                    “When our attention is focused inward at the depth of our inner being, in the context of the wholeness of our life, resources for a profound knowledge of life become accessible to us.”
  A Journal Workshop - p. 9.
                  </p>

                  <h6>Inward Focus </h6>
                  <p>
                   The focus of our work is inward. Progoff describes the atmosphere in which we work:
                  </p>
                  <p>
                   “The primary atmosphere of human beings...it is within them psychologically.
                    It is to be found in ...their underlying feelings regarding themselves and their fellow persons....their potentialities and the resources upon which they can draw.” (Symbolic and the Real p-12)
                  </p>
                  <p>
                   The exercises are designed to help you access and make tangible your feelings and experiences.
                  </p>

                  <h6>Privacy</h6>
                  <p>
                    In a group workshop, you have total privacy so that you are safe to write what comes to you.
                     No one will read or comment on what you have written. There is no group discussion or sharing.
                      You will not wear name tags, nor introduce yourself in the workshop.
                    Naturally, people socialize at the breaks but you should not feel any pressure to share the intimate details of your life.
                  </p>
                  <p>
                   We are seeking to protect the space and freedom for each person to work with the most intimate areas of the lives in an open and honest way.
                    An atmosphere of privacy honors the integrity of each person’s inner process, allowing them to feel the depth of their own experience.
                  </p>

                  <h6>Group Setting</h6>
                  <p>
                     Working in a group atmosphere, with the protections of privacy, provides the outer support - a sustaining atmosphere to draw strength from the presence of others.
                      One participant summed it up best by saying:
                  </p>
                  <p>
                    This solitary work we cannot do alone.” (AJW, p. 34)
                  </p>
                  <p>
                    The presence of others, each working privately in their own lives, deepens the atmosphere, creating a silent psychic energy or support that we do not feel when we are working alone at home.
                  </p>
                  <h6>Readings</h6>
                  <p>
                     From time to time, people in the group will read voluntarily from their <em>Intensive Journal</em> workbook.
                     Reading aloud can evoke emotion, bringing you further in contact with your emotions and life experiences.
                     The leader may provide non-judgmental feedback to help the reader connect with their own process using the method. Other people in the room naturally hear the reading, but they do not comment on the reading.
                     Please remember that there is no pressure to read.
                  </p>
                  <p>
                    “Each existence is engaged in finding the way of life and of being that will be true to its own nature.” (AJW, p 34)
                  </p>
                  <h6>Safety</h6>
                  <p>
                  Over the years, we have seen how participants can work through powerful issues using the <em>Intensive Journal</em> method.
                   You do as much as you can and then, if necessary, stop. You are never pressured to keep going.
                   The method has a self-regulating mechanism that helps you know how far to go.
                    The structure of the exercises, combined with the non-judgmental approach and support of the group, creates a non-threatening atmosphere from which it is possible to work through issues at one’s own pace.
                  </p>
                  <p>
                  Further, we work through individual issues within the context of our entire life using Progoff’s “whole-life” approach.
                  Issues tend to become more manageable when they are viewed in the context of our entire life experiences, talents and resources.
                  Since our lives are evolving over time, we approach our lives as a continual unfolding process.
                  We can revisit issues at a later time, reviewing what we have written and then building from that standpoint.
                   We don’t have to pressure ourselves to resolve an issue right away but rather let it unfold and reveal itself to us over time.
                  </p>
                  <h6>Progressive Deepening</h6>
                  <p>
                   As you work in the method, you can move to progressively deeper levels in your unfolding life process.
                    Your mind is focused inward, away from the distractions of daily life and preconceived ways of thinking.
                     The continuous experience enables you to go to deeper levels as you connect with your inner process.
                    For this reason, it is desirable for the workshop module which is typically 12 hours in length to be held on consecutive days in settings that respect quiet.
                  </p>
                  <p>
                   (The maximum daily length of instruction is six to seven hours.)
                  </p>
                  <h6>Neutrality</h6>
                  <p>
                   Neutrality is an important principle in our program.
                   We do not want to bias the process taking place inside you.
                    You will see this concept implemented in a number of ways.
                  </p>
                  <p>
                   Progoff emphasizes the concept of “passive receptivity,” recording everything that comes to mind, including materials that we may at the time view in a “negative” light, or materials that cause pain as we record them.
                   We are a reporter on our lives, recording all that comes to us without censoring, judging or filtering our thoughts.
                   This process allows us to access and work with our entire life experiences over time.
                  </p>
                  <p>
                   The leader will not seek to inject their personality or belief system into the workshop.
                   Their function is to guide you through the method, staying in the background, so as not to bias or interfere with your own process.
                  </p>
                  <p>
                   Our Entrance Meditation&trade; readings that are designed to quiet and center us, are neutral in terms of content.
                   The meditations contain images of nature; we are not seeking to implant any specific dogma, religion or thought process in you that would interfere with your process.
                  </p>
                  <p>
                   The exercises are designed with minimal terminology, staying true to the goal of not influencing your own process.
                  </p>
                  <h6>Silence</h6>
                  <p>
                   There are periods of silence during which you are writing and applying the <em>Intensive Journal</em> exercises to your life.
                   The silence is necessary for you to relax and access deeper levels within yourself in a neutral way.
                  </p>
                  <p>
                   As you can see, the atmosphere required for an <em>Intensive Journal</em> workshop is unique and central to the growth process.
                    It is more readily maintained away from the distractions of our daily life in the sanctuary of the workshop atmosphere.
                    Progoff summarizes the workshop atmosphere as follows:
                  </p>
                  <p>
                    “The experience of a Journal Workshop is like entering a sanctuary... a protected situation safe from the outer pressures of the world in which an individual can quietly reappraise their relation to their lives.
                     The procedures that are followed through the workshop give them a methodology to use....the presence of others in the workshop, each exploring the individuality of their own life history, builds an atmosphere that supports and strengthens...” (AJW - p. 17)
                  </p>


