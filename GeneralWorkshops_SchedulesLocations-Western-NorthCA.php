                        <p>+ Life-Study module is distinct from the first three modules. In the first three modules,
                            you are learning how to use the <em>Intensive Journal</em> method in your own life. In
                            Life-study, you are first studying the life of a person whose life has meaning for you and
                            whose life is complete - they are deceased. Life-Study requires advance preparation -
                            reading books, seeing films, etc. so that you have a basis from which to keep the Journal
                            on behalf the person you are studying. You should not have an emotional attachment or bias
                            about this person.</p>
                        <p>You are experiencing their life from an inner perspective as opposed to from an intellectual
                            perspective. Through this experience, you can experience the challenges and processes that
                            your subject experienced in developing their lives so that you can learn lessons for your
                            own life.</p>
