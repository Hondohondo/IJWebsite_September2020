                        <h6>How to select an online workshop</h6>

                        <p class="u-margin-bottom-small-1">
                            We are offering online workshops through Zoom that provide you with a range of options to best suit your needs.   Any module that has the letters "ONL" designates that the workshop is being held online. Example: LC-ONL is a Life Context module that is held online.    Several factors to consider are:
                        </p>

                        <p class="u-margin-bottom-small-1">
                           <b>  Number of modules.</b> There are 3 core modules (Life Context, Depth Contact and Life Integration) that must be taken in sequence.  You may wish to sign up for one module or the entire series.   Briefly,

                        </p>
