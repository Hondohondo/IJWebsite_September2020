                  <h1>Online Workshops Through Zoom - Overview</h1>

                  <p>
                  Due to the COVID-19 virus, we will be offering our <em>Intensive Journal</em> workshops online through the Zoom teleconferencing service.  Additional details will be announced on our website and through our publicity as we gear up for this major changeover.  I ask for your patience and understanding during this process.
                  </p>

                  <p>
              As you know, we have stressed the value of in-person workshops, often at retreat centers, as a way to give you the pure experience of working in your life away from your daily routine.  These workshops provide a progressively deepening atmosphere and power of the group experience to help propel individuals forward, with the instruction of a certified presenter, and the discipline of being away from daily distractions to focus on applying our process.
                 </p>

                 <p>
              However, given the COVID-19 virus, we cannot continue to offer our in-person workshops at this time.   Many of our participants and certified presenters are in the vulnerable age group where attending such a conference would jeopardize their health and safety.   The fact that people can have the virus without any symptoms creates additional complications and uncertainty.  Therefore, making our programs available through Zoom is the best option at this point.
                  </p>

<h6 class="u-margin-bottom-small-1">I can share with you some of our policies as we implement this plan:</h6>

                  <p>
            <strong>1.  Basic features. </strong>  The workshops will be scheduled and held just as an in-person workshop would be.  In other words, for workshops directly sponsored by Dialogue House, you will register with Dialogue House.  We will ship you an Intensive Journal workbook with paper and provide you with details about how to access the meeting.  While the meeting is held through a computer, the actual work in the process that you do is by hand in the workbook as before (unless you have a genuine physical disability).  The workshop will be held only during the designated hours.  For example, if the hours are 9 am-12 pm and 2-5 pm, those are the hours when you will connect into the conference.  I stress those are the only hours.  It is not a webinar where you can view it whenever you choose.
                  </p>

                  <p>
                  <strong>  2.  Privacy.</strong>  Privacy will be maintained.  Participants will see each other, though you have the option to not be seen.  Your full name will not be shared.  We will try to create an atmosphere, which we call the power of the group experience, one of the benefits of our program.  Participants can voluntarily read out loud.  If someone wants to ask a question that no one else will hear (analogous to an in-person workshop where a participant privately consults with a leader either in the room or by stepping outside the room), that is possible as a feature of Zoom.
                  </p>

                  <p>
                  <strong>  3.  "Locations."  </strong>We offer about 25 workshops and retreats around the United States, Canada and overseas.   I hope to maintain that concept of offering our program on multiple dates that are conducted by various leaders.  I do not contemplate one centralized workshop.  We want to retain the feel of a local workshop - the local leader for that area with, if possible, most participants from that region.  Some people repeat workshops and a number of persons return to the program each year.  It would be good to continue to achieve that local feel with an ongoing group.  Naturally, some participants may wish to experience the program with other leaders, and as always, anyone from around the world can enroll.
                  </p>

                  <p>
                <strong>4.  Size of the group.</strong>  I plan to limit the number of participants to approximately 25-30, and since we like to create a local feel to the group, I will give priority to local people.  There will be plenty of opportunities to participate in workshops offered on a variety of dates.
                  </p>

                  <p>
              <strong>  5.  Dates.</strong>  The dates will be scheduled to give you at least 2-3 months' advance notice.
                  </p>

                  <p>
                <strong>  6.  Fees.</strong>  Our standard fees will be about $200 per workshop, the same as we charge at in-person workshops (before adding commuter / room and meal charges).
                  </p>

                  <p>
                <strong>7.  Future of in-person workshops.</strong>  I hope eventually we can return to offering a broad range of in-person workshops and I envision that a small number of in-person workshops may still be offered, at the discretion of our certified presenters.)
                  </p>
                  <p>
                  <strong> 8.  Recording.</strong>   Recording any part of the program is strictly against the rules, and this policy remains in place under all circumstances.
                  </p>
                  <p>
              <strong>9.  Documentation and training to use the Zoom teleconferencing service.</strong>       I am preparing documentation for using Zoom and our policies and procedures for participating in an <em>Intensive Journal</em> workshop through Zoom.  You will need to perform this training before the workshop.  I hope to have a technical assistant available should technical difficulties arise during the workshop.   If you are especially skilled in Zoom, I encourage you to contact me to discuss how you can help us in this regard.
                  </p>

<p>Thank you for your involvement as we move forward together in this exciting endeavor.</p>
