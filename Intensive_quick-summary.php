                  <h1>Quick Summary of the <em>Intensive Journal</em> Program</h1>

                  <p>
                    Welcome to the <em>Intensive Journal</em> Program!   This section is designed to give you sufficient information about the <em>Intensive Journal</em> method and what happens at a workshop so that you can decide whether you want to attend a program.  Naturally, you may wish to read the additional information about the method and workshops on our website.
                  </p>

                  <p>
                The <em>Intensive Journal</em> method is a series of integrated writing exercises about different areas of your life (i.e. personal relationships, body and health, life history, dreams and imagery, meaning in life, etc.) that are designed to help you get to know yourself better.  We are laying a foundation on our lives, and then over time, working through issues within the context of our entire life.
                 </p>

                 <p>
                Our program is different than anything you have experienced.  It is based upon Dr. Progoff’s theories of human development that he then implemented in the <em>Intensive Journal</em> method.  You do not need to know the theory - just how to use the method.
                  </p>

<h6>Work in a Deepening Atmosphere with Total Privacy</h6>

                  <p>
                    The atmosphere in which the method is conducted is critically important.  A certified instructor will describe an exercise, and then there is a period of quiet where you are writing and applying it to your life.  You work in total privacy - no one comments on your life or vice versa.  The workshop is a progressively deepening atmosphere away from your daily routine where you connect with your inner process and access what is inside you using the <em>Intensive Journal</em> exercises.  The leader is there to guide you through your process and not influence your process.
                  </p>

                  <h6>
                    Attend Up to Three-Core Workshop Modules
                  </h6>

                  <p>
                    There are three core modules that comprise the <em>Intensive Journal</em> method.  Each part is typically 12 hours in length spread over two days (about 6 hours per day plus breaks for meals).   The program is paced with breaks to recharge and maintain your energy.  The modules are:
                  </p>

                  <ul>
                    <li>
                    <strong> Part 1 (Life Context)</strong>  covers about half of the exercises in the <em>Intensive Journal</em> workbook and is a very solid experience by itself. <em>No prerequisite.</em>
                    </li>
                    <li>
                    <strong>Part 2 (Depth Contact)</strong>   covers the remaining half of the exercises, primarily in the areas of dreams and imagery and meaning in life. <em>Prerequisite: part 1.</em>
                    </li>
                    <li>
                  <strong>Part 3 (Life Integration / Journal Feedback process)</strong>   helps you develop the material that you started in the first two modules using Dr. Progoff’s feedback process.  It also helps you see connections between different areas of your life because we know that one area impacts and relates to another. <em> Prerequisite: part 2.</em>
                    </li>
                  </ul>

                  <p>
                    As noted, the workshops must be taken in sequence.  Since we often offer more than one module back to back at our program, you may wonder how many workshops you should attend.  It all depends upon the individual - typically about two-thirds of the attendees enroll in the entire program.  Sign up for what you feel safe doing.
                  </p>

                  <h6>Obtain Possible Benefits: Insights, Perspectives....</h6>

                  <p>
                    Attendees often obtain unpredictable insights about their lives, sometimes at the most surprising times or areas of their lives.  We are receptive to what comes to us throughout the process; often insights come indirectly from other areas of our lives.  We suspend judgment and do not create any deadlines or expectations for resolving an issue.  Answers to questions we have will come to us when they are ready to reveal themselves.
                  </p>

                  <h6>
                    No Advanced Preparation Necessary
                  </h6>

                  <p>
                  There is nothing to prepare in advance of the workshop.  Just come, be relaxed, and follow along.  If you are aware of other methods, please leave them aside during the workshop and give the <em>Intensive Journal</em> method a chance to work in your life.
                  </p>

                  <p>
                    You receive the <em>Intensive Journal</em> workbook (3 ring binder with tabs in it) as well as paper and a pen.  The main book for the program - At a Journal Workshop (Rev. Ed. 1992) is available for purchase at the workshop; it is used as a reference tool after the workshop.
                  </p>

                  <h6>
                    Participants With Different Backgrounds and Interests
                  </h6>

                  <p>
                    People of different interests and backgrounds attend our program.  The common theme is that they are there to work in their lives using our method.  The program is geared for the beginner although people repeat the modules from time-to-time to work in their lives in the atmosphere of the workshop.
                  </p>

                  <h6>
                    Skills and Interest in Writing Unnecessary
                  </h6>

                  <p>
                    You do not have to like to write nor be a good writer.  We have people who are barely literate who attend our program and gain a great deal out of the experience.  You are the only one  who reads what you have written.  You are also not writing your life story - you are simply writing what comes to you in a reflective atmosphere as you apply the exercises to your life.
                  </p>

                  <h6>
                    Often Held at Retreat Centers
                  </h6>

                  <p>
                    We hold many of our programs at retreat centers.  These centers respect quiet and provide a conducive atmosphere for our program.  The <em>Intensive Journal</em> program is neutral with regard to any particular religious doctrine - we offer a neutral process to help you get in touch with your beliefs.
                  </p>

                  <h6>
                    Held Internationally Throughout the Year
                  </h6>

                  <p>
                    We offer workshops in the United States, Canada, and overseas (Europe and Australia) throughout the year. Our schedule is updated periodically; check our calendar on our website.
                  </p>

