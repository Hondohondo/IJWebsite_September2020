              <h1>3 Core Workshops</h1>

                  <h6>Part I: Life Context (LC): Foundations of the <em>Intensive Journal</em> Method. Prerequisite: None. Length: 10-12 hours </h6>

              <ul>
                <li>Learn basic rules for using the <em>Intensive Journal</em> method and principles of holistic depth psychology that underlie the method.</li>
                <li>Use Life Positioning exercises to describe feelings and events about your nursing career and to develop the continuity in your professional development.</li>
                <li>
                  Apply Entrance Meditation™ techniques to reduce tension and access inner processes.
                </li>
                <li>
                  Use and integrate diary techniques into the <em>Intensive Journal</em> process.
                </li>
                <li>
                  Apply I-Thou model of dialogue techniques to develop bereavement skills, work through work situations, and health/body issues.
                </li>
                <li>
                Recall and develop experiences of deep emotion and meaning.
                </li>
              </ul>


                  <h6 class="u-margin-top-small-2">Part II: Depth Contact (DC): Symbolic Images and Meaning.
Prerequisite: LC workshop. 10-12 hours</h6>

              <ul>
                <li>Use dialogue techniques to explore events or situations in the workplace.</li>
                <li>Apply non-analytical methods for using symbolic material.</li>
                <li>
                  Recall and develop significant experiences about your profession.
                </li>
                <li>
                  Reappraise a meaningful event that can provide important lessons.
                </li>
                <li>
                  Apply dialogue techniques to work through issues relating to the health care industry.
                </li>
                <li>
                  Define and organize issues to synthesize beliefs and priorities.
                </li>
              </ul>

                  <h6 class="u-margin-top-small-2">Part III: Life Integration (LI): the Journal Feedback™ Process: Integrating the <em>Intensive Journal</em> Process. Prerequisite: LC & DC, or LE workshops. 10-12 hours</h6>

              <ul>
                <li>Explain and define the principles for using the Journal Feedback process.</li>
                <li>Use the daily log function to stimulate the Journal Feedback process. </li>
                <li>
                  Develop and further organize daily feelings about the workplace through advanced feedback techniques.
                </li>
                <li>
                 Apply the Journal Feedback Process to the Dialogue Dimension exercises.
                </li>
                <li>
                   Use dreams and imagery as leads to other sections using the Journal Feedback Process.
                </li>
                <li>
                   Apply the Journal Feedback Process to the Meaning Dimension exercises.
                </li>
              </ul>

