                        <h1>B.  Discounts and New Workshops</h1>


                        <h6>  1.   Small Group Discounts</h6>
                        <p>
                            If you are associated with a small group (at least 6 people) who would like to attend one of our existing workshops where Dialogue House conducts the registration (most workshops), we can offer a group discount to each participant.  People within the group can attend different workshops as long as Dialogue House handles the registration.
                        </p>

                        <h6>2.  Host an Introductory Session</h6>
                        <p>   Dialogue House conducts 1-3 hours introductions of the Intensive
                            Journal method for organizations that can organize a group of 12-15
                            people.  Participants learn key principles of the method, experience
                            sample writing exercises and then can elect to enroll in a full-length
                            (2 day) workshop to continue learning the process.  Please urge an
                            organization to host an introductory session and then discuss with
                            Dialogue House</p>

                        <h6>3.  Host a Full-Length Workshop for the General Public</h6>
                        <p> Organizations that can organize a group of 12-15 people can host or
                            sponsor a full-length (2 day) workshop.  Sponsors promote the program
                            and register participants, and in return, earn a percentage of the revenue.</p>

                        <p>
                            We can use your help in obtaining an introduction to an
                            organization that may wish to sponsor.  Possible sponsors include growth
                            centers, churches, counseling centers, writing groups, etc.- any group
                            that can organize a group.
                        </p>

                        <h6> 4.  Host a Full-Length Workshop for an Institution</h6>
                        <p> Since the <em>Intensive Journal</em> method can help practically anyone, we have
                            conducted our program for audiences that are served by various
                            government and social service agencies.  We are looking to add workshops
                            in the health care field (addiction,depression, cancer survivors),
                            including with caregivers.   In the area of spirituality, we have
                            conducted many programs in seminaries and for sabbatical programs. The
                            possibilities are practically endless.  The fees that are charged are
                            much less than for a public workshop.  The scheduling of hours for the
                            program are also extremely flexible
                        </p>

