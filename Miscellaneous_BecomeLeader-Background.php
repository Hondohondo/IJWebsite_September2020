                  <h1>Background Information</h1>

                  <p  class="u-margin-top-small-2">
                   The application process is primarily designed for individuals who intend to become trained and certified as a leader to conduct <em>Intensive Journal</em> workshops. Key issues such as the qualities of an effective leader, the appropriate time to apply for consideration, and other prerequisites (e.g. prior workshop attendance) are discussed in detail.
                  </p>

                    <div class="ordered-list">

                    <ol class="ordered-green">

                        <li class="list-green">Nature of the Training Program</li>
                        <p>
                        The training program is designed to help you deepen your knowledge of both the <em>Intensive Journal</em> method and the principles and theories of holistic depth psychology upon which it is based. You are being trained to represent Dr. Progoff’s organization in presenting the broad scope of his work.
                        </p>
                        <p>
                      The study program complements your use of the <em>Intensive Journal</em> method, including your experiences at our workshops. This is the crucial beginning point. Through your experiences in using the <em>Intensive Journal</em> process, you will know first-hand how the method works and the reasoning behind its structure and processes.
                        </p>
                        <p>
                        The study guide for each step provides you with a detailed outline of the subject matter to be mastered. Although you will be studying independently, you will have the support of a structured guide, feedback from our training director who will respond to your reports and answer any questions that you may have, and ongoing support from the senior consultant with whom you will be working.
                        </p>
                        <p>
                       Self-study is an important component for learning the <em>Intensive Journal</em> method. This method is not well-suited to the lecture format which is common in many training programs. There is no substitute for the ongoing workshop experience and careful review of the required readings. Dr. Progoff’s theories and approaches require detailed study and reflection to be fully understood. Being able to describe the method using appropriate language and reasoning is a skill that is acquired over time through ongoing study and use of the method.
                        </p>

                                <li class="list-green">When Should I Apply to Become a Leader?</li>
                        <p>
                     We recommend that you work in the method in your own life for several years and attend many workshops, including our summer workshop program where the three core modules are presented in a consecutive series. Then, over time, you may begin to feel that you have a calling to teach this method. We encourage you to discuss your interest with various leaders.
                        </p>
                        <p>
                      We ask that you carefully consider whether your temperament and skill base is appropriate for serving as a leader, given the unique qualities of the method and the significant commitment of time and energy. (Please see the section below entitled: “Qualities of an Effective Leader”).
                        </p>


                        <li class="list-green">Qualities of an Effective Leader</li>
                        <p>
                     There are several important characteristics for being an effective leader of the <em>Intensive Journal</em> method:
                        </p>

                        <ul>

                            <li>
                                Knowledge of the method. Leaders must have a detailed knowledge of the method and be able to articulate the principles in a clear and concise manner.
                            </li>
                            <li>
                                Ongoing learning. Learning the <em>Intensive Journal</em> method is analogous to learning an instrument; it takes time and ongoing use from which you experience and validate first-hand how the method operates. Our leaders appreciate the profound nature of Dr. Progoff’s work and study it over time. Certification is only one step in a continuing learning process.
                            </li>
                            <li>
                                Interpersonal skills. Effective leaders display an empathy and warmth, relating well to people of different backgrounds and interests. They are skilled at handling sensitive situations such as in responding to questions or readings. Our leaders are intuitive and have an artistic and creative talent. They have a maturity about life, and although we all have areas to improve, it can be said that they have their lives together.
                            </li>
                            <li>
                                Communication skills. An effective leader can articulate both the exercises and principles. They have an energy and confidence with a dynamic quality that at times can carry the group. However, their personality does not intrude into the workshop.
                            </li>
                            <li>
                               Dedication/commitment to the method. Our leaders have a deep and abiding interest in the effectiveness of Dr. Progoff's program, valuing it as one of the primary tools that people can use in their personal and spiritual growth. Both the study program and the actual conducting of workshops require a commitment of time and energy to the Method.
                            </li>
                            <li>
                                Representation of Dialogue House. As a leader, you will be representing Dr. Progoff’s work and his organization. This relationship requires a different way of thinking since you are presenting another person’s work.
                            </li>

                        </ul>


                         <p>
                     There are several important characteristics for being an effective leader of the <em>Intensive Journal</em> method:
                        </p>


                        <li class="list-green">Background of the Leader</li>
                        <p>
                        Our leaders have a variety of backgrounds. They are educators, ministers, artists, and counselors. It is not a requirement to be a psychologist or to have a counseling background for becoming certified to conduct our programs.
                        </p>


                        <li class="list-green">Distinguish Other Interests</li>
                        <p>
                       We ask that you distinguish an interest in becoming a leader from other needs. Three common examples are:
                        </p>

                        <ul>

                            <li>
                                Organizing a workshop. You may want to have a workshop in your area. Please contact us and we can work with you to find an organization to host the program.
                            </li>
                            <li>
                                Interest in sharing the method. Many people who have a positive experience at a workshop desire for other people to experience the method. Teaching this method can appear deceptively simple. Again, the quickest and easiest way for your community to experience the method is by organizing a workshop in your area or referring people to an existing one.
                            </li>
                            <li>
                               Supplemental income. We regard conducting <em>Intensive Journal</em> workshops as a supplemental source of income for professionals. It should not be viewed as the main source of income for any of our certified consultants.
                            </li>
                        </ul>


                          <li class="list-green">Attendance at Workshops</li>
                        <p>
                        Attending <em>Intensive Journal</em> workshops is critical for learning how to use the method. Therefore, we expect you to have attended many workshops prior to applying to our training program and to continue to attend our programs while you are in training and after you become a leader. (Tuition discounts are offered to those in training and to leaders.) At minimum, you need to attend at least one continuous retreat of the three core <em>Intensive Journal</em> workshops (Life Context, Depth Contact and Life Integration). You will need to attend at least two more three-part continuous series (typically held in the summer) prior to completing the training program. You receive tuition discounts once you are enrolled in the training program.
                        </p>

                        <p>
                        We expect you to travel to workshops if they are not in your area. We place a great deal of emphasis and effort on offering the three part series in the summer at sites around the country.
                        </p>

                        <p>
                       We expect you to take additional <em>Intensive Journal</em> workshops which will help you to deepen your experience and familiarity with the Method as your understanding of the principles grows.
                        </p>


                          <li class="list-green">Timing and Deadlines to Complete the Study Program </li>
                        <p>
                        The program is designed to help you move through the material in an organized and methodical way. While we understand that you have other commitments, your involvement in this training process needs to be given priority. Otherwise, we find that people stagnate in the program.
                        </p>

                        <p>
                       Therefore, prior to beginning the first study guide, you set a timetable for submitting your reports. We find that if you set aside a certain block of time each week, you will be able to submit the reports on a monthly basis and complete the study program.
                        </p>


                        <li class="list-green">Reference Letter </li>
                        <p>
                       We require a reference letter from one of our leaders as part of the application process. This letter is very important and a general reference from someone who is not a leader cannot be substituted.
                        </p>

                        <p>
                       Please consult with a leader at a workshop about your interests and background. A leader may need to work with you in several workshops prior to making a judgment on your suitability for the training program.
                        </p>

                          <p>
                     Once a leader is willing to complete the reference, you should provide him/her with the attached reference form. The leader will then complete and send the reference form to Dialogue House Associates directly.
                        </p>

                        <p>
                     We have found that teaching this method requires a unique set of skills that many people who are otherwise intelligent and competent may not possess. The reference letter from a leader helps us evaluate your compatibility with conducting workshops.
                        </p>

                        <p>
                        Accordingly, it is advisable to continue to work in the process, take workshops, and, over time, connect with various leaders in the program.
                        </p>

