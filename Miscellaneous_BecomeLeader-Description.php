                          The Progoff&trade; Methodology: Holistic Depth Psychology and the <em>Intensive Journal</em> Method:
 An Integrated Study Guide of Theory and Exercises (Step 1)


                        </li>
                        <p>
                         In this study guide, we cover Progoff’s theories of psychological growth and how they were implemented in the <em>Intensive Journal</em> method.
                         Progoff was a pioneer who had a unique vision for the role of psychology.
                         Progoff was a synthesizer of ideas from several psychologists, philosophers and theologians such as Carl Jung, Martin Buber, Otto Rank, Jan Christian Smuts, etc., and we study Progoff’s interpretation of their theories.
                          You then study Dr. Progoff’s philosophy of holistic depth psychology, including the concept of depth and the processes of psychological growth.
                        </p>
                        <p>
                       Each dimension of the <em>Intensive Journal</em> method is studied in detail, including the theory upon which it is based, the purpose of the <em>Intensive Journal</em> sections, their applications and interrelationships,
                        and <em>Intensive Journal</em> techniques, such as the principles of the Journal Feedback TM method.
                        The emphasis is on conceptual understanding and being able to articulate the principles in a manner faithful to the method.
                         Through your workshop experiences, you can validate how the method is working in your own life.
                        </p>
                        <p>
                          The required readings are obtained from a broad cross-section of Dr. Progoff’s writings as well as articles and audio cassettes.
                           This study guide requires 16 reports; you should submit monthly reports so that you complete the assignments within two years.
                          It is the equivalent of about one college course.
                        </p>

                                <li class="list-green">Certification: Becoming a Journal Consultant (Step 2)</li>
                        <p>
                     You will focus on studying areas relating to conducting an <em>Intensive Journal</em> workshop and how we expect you to work within our organization.
                      Issues relating to leading workshops include how to handle issues that arise in a workshop, assessing your communication skills, responding to readings, etc.
                       You will become a provisional journal consultant who is authorized to conduct <em>Intensive Journal</em> workshops for smaller scale (not public) audiences.
                        Your performance will be reviewed to ensure that it meets the high standards that have been maintained in the Progoff <em>Intensive Journal</em> Program.
                        </p>
                        <p>
                          Depending upon your location and the proximity of a senior leader to you, we will work with you to arrange a workshop, whether in your area or closer to the senior leader.
                          The person you work with will be asked to make suggestions to you about your further development as a journal consultant.
                        </p>

