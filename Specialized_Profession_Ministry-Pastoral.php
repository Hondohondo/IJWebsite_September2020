                  <h1>Chaplains & Clinical Pastoral Education</h1>

                  <p>
                The <em><em>Intensive Journal</em></em> method can serve a valuable role in achieving the objectives set forth by the Association of Clinical Pastoral Education in your clinical pastoral education program. Our program provides valuable skills for those who then move forward to become chaplains in an institutional setting.
                  </p>

<h6 class="u-margin-top-small">
Use a Leading Methodology For Writing and Self-Reflection
</h6>
<ul>
  <li>Apply an integrated system that deepens the reflection process in a sustained contemplative atmosphere for awareness and perspectives.</li>
  <li>Utilizing sound theoretical principles of psychology, the Progoff methodology provides proven techniques to use in your institution.</li>
</ul>
<h6 class="u-margin-top-small">
Foster Self-Awareness in the Workplace
</h6>
<ul>
  <li> Use specialized exercises that are designed to make tangible and accessible details of issues for further assessment and review.</li>
  <li>Learn ways to approach issues from different perspectives to be more effective working in an interdisciplinary team environment.</li>
  <li>Become more attentive and observant of situations as you work through details of issues over time.</li>
  <li>Access deeper-than-conscious levels, a key way to build intuitive capacities.</li>
  <li>Become more expressive through the use of the writing exercises.</li>
</ul>




    <h6 class="u-margin-top-small">
  Develop Ministry and Pastoral Counseling Skills
    </h6>
<ul>
  <li>Use the method to access capacities and develop skills as a minister. The program is recognized as an effective tool for ministry, having a strong emphasis upon spiritual growth. Many program leaders have a background in ministry and related areas.</li>
  <li>Access beliefs using a neutral process without dogma, well-suited for working in an interfaith setting.</li>
  <li>Learn an important complement to counseling, providing approaches that are especially helpful for people in crisis.</li>
  <li>Gain awareness about how attitudes, values, assumptions, strengths and weaknesses affect pastoral care ministry.</li>
</ul>

