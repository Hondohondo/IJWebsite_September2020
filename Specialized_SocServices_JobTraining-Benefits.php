                  <h1>Benefits of the <em>Intensive Journal</em> Program</h1>

                  <p>
          Some quotations in this section are from a report entitled: "New York State Department of Labor Job-Training Program: Applying the Progoff <em>Intensive Journal</em> Method" and the trainees who participated in the program. References are cited as "Trainee" or as "NYS-DOL Report". The article is available on this website in the section of Job Training called Articles.
                  </p>

                  <h6>Developing Vocational Interests and Skills</h6>
                  <p>
                As trainees realize their potential, they become empowered and gain greater self-confidence. They become more interested in obtaining and developing a vocation. Once they can begin to visualize themselves moving forward, their inner capacities propel them forward and their true inner potential can be realized.
                  </p>

                  <div class="quotes">

