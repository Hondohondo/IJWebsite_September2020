                  <h1>Enhancing Job Performance</h1>

                  <p>
                Some quotations in this section are from a report entitled: "New York State Department of Labor Job-Training Program: Applying the Progoff <em>Intensive Journal</em> Method" and the trainees who participated in the program. References are cited as "Trainee" or as "NYS-DOL Report". The article is available on this website in the section of Job Training called Articles.
                  </p>
                  <p>
                The <em>Intensive Journal</em> method can be used to increase the effectiveness of a job training program. Trainees develop new perspectives on their lives, giving them the ability to grow as individuals. They can become more productive, responsible and mature. Through this transformation in attitudes and behavior, their all-important interpersonal skills are strengthened.
                  </p>

                  <div class="quotes">

                    <blockquote>
              "As an indication of the 'quality of work' aspect of the program, the geriatric residents increasingly made it a point to request that trainees of the Dialogue House program take care of them, rather than the regular employees."
                </blockquote>

                    <p class="right-span">NYS-DOL Report</p>
                      </div>

                  <p>
                    This improvement is especially helpful for individuals who are difficult to train, and who often exhibit uncooperative and lackadaisical behavior. These individuals who often require special attention and training can be changed at a fundamental level using the <em>Intensive Journal</em> method. After all, performance in entry level jobs is determined heavily by an individual's attitude, reliability, interpersonal skills, and interest and determination in succeeding. The Progoff methodology can change a person in a fundamental way to increase the success rate of these difficult to train individuals.
                  </p>

                  <div class="quotes">

                    <blockquote>
            "Pride in self leads to pride in work,... the tangible experience of knowing that you are a person living a valid life is the essential factor in bringing about an increase in the quality of work in an organization."
                </blockquote>

                    <p class="right-span">Ira Progoff</p>
                      </div>

                      <h6>
                        Fostering Self-Improvement
                      </h6>

                  <p>
                By connecting with their unique life experiences, trainees are able to access and develop their inner strengths and capacities, and gain life-changing insights and perspectives. Trainees move from a state of denial and avoidance of painful feelings and experiences to greater recognition and acceptance. They learn to trust their own inner process and are better able to address powerful issues that had previously resulted in anger, hostility and frustration.
                  </p>

                  <div class="quotes">

                    <blockquote>
          "I am a person who doubts myself very much. Now with the Journal I don't doubt myself, because I know I can do it and I'm going to do it."
                </blockquote>

                    <p class="right-span">C.L. (Trainee)</p>
                      </div>

                      <h6>
                        Improved Attitudes
                      </h6>

                  <p>
                    Changing the attitudes of participants is critical for their success in the training program, especially since they are coming from a tough living environment.
                  </p>

                  <div class="quotes">

                    <blockquote>
          "At Isabella House, it was frequently observed that the young men and women who were lackadaisical and diffident when they entered the program became enthusiastic in taking care of the older patients, volunteering to do much more than was required of them."
                </blockquote>

                    <p class="right-span">NYS-DOL Report</p>
                    <blockquote>
          "Both department supervisors and residents have commented on the change of atmosphere since the trainees joined the staff. They use words such as 'enthusiasm,' 'interest,' 'kindness' and 'a sense of the joy of life.'"
                </blockquote>

                    <p class="right-span">NYS-DOL Report</p>
                      </div>

                  <p>
              The Progoff methodology employs several procedures that help trainees change their attitudes. The atmosphere of the workshop is supportive and respectful of the traineesâ€™ unique process. Emphasizing privacy, a nonjudgmental approach provides a safe environment from which to explore their lives, including many powerful and emotional issues.
                  </p>

                  <p>
                    The Progoff methodology helps trainees avoid preconceived ways of thinking. They do not analyze their lives, which results in circular ways of thinking. Instead, to break free, they follow the <em>Intensive Journal</em> procedures, working at a deep inner level in an non-analytical way. They are able to evoke and draw forth capacities and resources from within to build their lives. The <em>Intensive Journal</em> exercises help make tangible experiences and emotions from which to gain awareness.
                  </p>

                  <div class="quotes">

