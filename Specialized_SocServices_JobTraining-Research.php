                  <h1>
                    Former Studies
                  </h1>

                  <h6>
                    New York State Department of Labor Study:
                  </h6>

                  <p>
                  <a href="articles/Learnmore_Articles_SocialServices-NYStateDepartOfLabor.php" class="page-link">New York State Department of Labor Job-Training Program:</a>   Applying the <em>Intensive Journal</em> Method by S.A. Sealy and T.F. Duffy. Abridged Version.
                  </p>

                  <p>
                    Dialogue House participated in an eighteen month on-the-job training program in New York City to recruit nearly 300 entry-level workers for a geriatric residence. The trainees were largely classified as “disadvantaged”– welfare recipients and chronic unemployed– many with low levels of education. In addition to on-the-job training and remedial education, trainees participated in ongoing <em>Intensive Journal</em> workshops.
                  </p>

                  <p>
                    The program was quite successful: 249 of the 286 people in training completed the program and were employed at the geriatric residence. After one year, 222 out of 249 who completed the training were still employed or had taken another job. Adding the <em>Intensive Journal</em> program to the traditional on-the-job training and remedial education programs was credited with significantly contributing to the above-average performance rates.
                  </p>

