                  <h1>Research Studies</h1>

                  <p>
                    Dialogue House is seeking to participate in independent research studies to determine the effectiveness of the <em>Intensive Journal</em> method in helping participants in a job training program. We are especially interested in measuring the short-term impact of using our method over a six to nine month period upon behaviors and attitudes of trainees.


