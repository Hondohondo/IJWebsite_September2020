                  <h1>Overview</h1>

<p>
Beginning in the early 1950s, Dr. Progoff was a leading proponent of the importance of spiritual development as an overall component of psychological healing and growth.
</p>

<p>
  Progoff wrote one of the leading introductory commentaries and translations on <em>The Cloud of Unknowing</em> by the 14th century anonymous monk as he describes how individuals can become connected to God.
</p>

<blockquote>
  "What is separating man from God is not any physical thing but the state of his consciousness.
More specifically, it is man's consciousness of his separateness, of his existence as an individual
apart from God that keeps him separated from God. To overcome this awareness of separateness, he must place himself in a psychological situation that weakens, and ultimately dispenses
with, the conscious guidance of his personal being. He must permit himself to drop into a
condition of unconsciousness, a condition of total unknowing. This encompassing state of
unknowing is the cloud of unknowing that covers him in darkness, dulls his awareness of his
separate existence, and provides the medium in which his union with God may take place." <sup>1</sup>
</blockquote>
