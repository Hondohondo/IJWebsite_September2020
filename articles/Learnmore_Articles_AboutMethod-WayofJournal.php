                  <h1>The Way of the Journal</h1>
                <em>
                  <p>from Psychology Today, March, 1981 by Robert Blair Kaiser </p>
                </em>
                <em>
                  <p>Within each of us is an underground stream of images and recollections that is nothing more or less than our interior life. Through keeping a special sort of "intensive" journal, Ira Progoff believes we can enter that stream. At his workshops, many secularized Americans rediscover the spiritual.  </p>
                </em>


                <p>
                Last year, thousands of Americans with no literary pretensions whatsoever started producing stories of surpassing interest that will probably never be published, or even read by their best friends. They were writing their own, often eye-popping, tear-evoking journals, under the direction of a tireless, tireless New York psychologist named Ira Progoff.
                </p>

                <p>
                  They wrote these journals in 392 workshops sponsored by colleges and universities across the land, by branches of the armed forces, by army hospitals and women's prisons, by groups of artists, priests, poets, business people, and engineers (and combinations of all of the above); they didn't enroll in them (for an average tuition of $70 each) because they felt they needed therapy, but because they wanted to put their lives in perspective and find in them some deeper meaning.
                </p>

                <p>
                  The <em>Intensive Journal</em> method comes from everywhere—and from nowhere, except the synthesizing mind of Ira Progoff. Because he is a psychologist who studied under Carl Gustav Jung and is one of the founders of the Association for Humanistic Psychology, people have a mistaken notion that he is either a therapist or the newest in a long line of gurus— like Abraham Maslow, Fritz Perls, and Werner Erhard—who helped people "actualize" themselves.
                </p>

                <p>
                  `'Wrong," says Felix Morrow, the editor of Progoff's last two books, At a Journal Workshop and The Practice of Process Meditation. "It's hard to get a handle on Ira. He's an original. He's not a therapist and he's not in the human potential movement. But if you're looking to see what ‘line’ he's in, I think you'd have to say he follows Martin Buber and Paul Tillich."
                </p>

                <p>
                  By many accounts, Buber and Tillich, who both died in 1970, were the 20th century's greatest philosopher-theologians. They were great because they refused to be confined by the traditions they grew up in—Buber was Jewish and Tillich, Lutheran—and because they were open to the full horizon of possibilities reachable by men and women with dynamic religious faith. Furthermore, they were able to communicate their vision to a wide group of followers.
                </p>

                <p>
                  Ira Progoff has the same vision and the same thrust: he is now very much like a philosopher-theologian himself, interested not only in helping people find meaning in their lives but also in making the world a gentler place. Progoff may never write anything as deep as Buber's I and Thou or as broad as Tillich's Systematic Theology. But he may be doing something even more important: working out a method that will help people find ultimate meaning, both for themselves and for others. That method is the <em>Intensive Journal</em> system, which many believe is a unique tool that contemporary men and women can use to make tangible the most elusive, most subjective parts of themselves—those subtle "intimations of truth" that give direction to their lives.
                </p>

                <p>
                  At the beginning of his book on process meditation, Progoff tries to explain what he is up to by recalling a story. When he returned to civilian life after World War II, he had a recurrent daydream. He wondered what might have happened to civilization if the Nazi's ritual book burnings had continued until all the recorded wisdom of mankind had been destroyed— all the bibles of the world, the Old and the New Testaments, the Tao-te Ching, the Upanishads, the Koran, and all the others. "If that happened," he asked himself, "what would befall mankind?"
                </p>

                <p>
                  Finally the answer came to him and in a very matter-of-fact tone: "We would simply draw new spiritual scriptures from the same great source out of which the old ones came." And soon another thought came: "If mankind has the power to draw additional spiritual scriptures out of the depth of itself, why do we have to wait for a Hitlerian tyrant to burn our bibles before we let ourselves create further expressions of the spirits?... Perhaps there are new bibles, many new bibles, to be created as the sign of spiritual unfoldment among many persons in the modern era. It may be . . . part of the further qualitative evolution of mankind."
                </p>

                <p>
                  There not only may be new expressions of the spirit, Progoff added, there must be. Why? Because we find that the old bibles of whatever tradition are being lost to moderns. Contemporary men and women live in a different way than Moses, David, Matthew, Mark, Luke, Paul, and all the rest; they have begun to think differently as well. Progoff's solution: mankind has to renew its old bibles, get in touch with the profounder meanings of life, which he claims are in everyone, "whatever their faith or lack of faith."
                </p>

                <p>
                  A member of the Teilhard Association for the Future of Man and a peripatetic scholar who is always on the move, always talking to people with ideas, Progoff sometimes sounds like an affable missionary of the mind who wants to enlist much of mankind, person by person, in the task of "extending the process of evolution." He believes that men and women who plumb the sources of meaning are automatically "building up the noosphere," that cloud of thought which, according to Pierre Teilhard de Chardin, the French Jesuit paleontologist, philosopher, and theologian, hovers over the earth and somehow provides the nurturing environment mankind needs in its march toward an omega point of fulfilling perfection.
                </p>

                <p>
                  For those who want to plumb the sources of meaning, Progoff likes to use a metaphor that many find helpful. He says that there's an underground stream of images and recollections within each of us. The stream is nothing more or less than our interior life. When we enter it, we ride it to a place where it wants to go. He says this is not a discursive method, not analytic: "There's no neat wrap-up; you don't end up with 'insight.' It's an event, and when it's happened, your life is different."
                </p>

                <p>
                  All of this may sound rather mystical. But then the mysticism gets terribly concrete, because everyone at a Journal workshop ends up with a workbook weighing several pounds, full of stories and recollections and often surprising new insights about the most fascinating mystery of all: themselves and their relation to the world around them. To produce a Journal, however, Progoff is quick to point out, "You don't need to be a mystic. All you need is a life. Almost anyone can do it."
                </p>

                <p>
                  A trip to Dialogue House, Progoff's cluttered third-floor headquarters in a Lower Manhattan office building, provides proof enough of that. Thomas Duffy, Progoff's director of advanced studies, opens his files to me and shows me reports from workshops across the land. In Morgantown, West Virginia, for instance, I see that Dr. Virgil Peterson conducted a Journal workshop for faculty members at the University of West Virginia. James Armstrong, a professor at Loyola University of Chicago, gave a Journal workshop at the annual convention of the American Holistic Medical Institute. For the past three years, Sister Maureen McCormack, a Sister of Loretto, has been giving Journal workshops to groups in Lakewood, Colorado, who are training for the lay ministry. An organization in Chicago called the Institute of Women Today has sponsored l 6 Journal workshops in six different women's jails and prisons in the Midwest.
                </p>
                <p>
                  In one experimental program at a New York City facility for the elderly, some 300 recruits from the city's welfare and unemployment rolls were enrolled in a Journal workshop as part of their on-the-job training as nurse's aides, dietary workers, security guards, maintenance men, and housekeepers. Most of them were either unlettered blacks from the South or recent immigrants from the Caribbean, people likely to fail in the big city and end up on its bulging welfare rolls. Ninety percent of them kept their journals over a six-month period (they met once a week), finished their training, and stayed on to perform their low-status hospital jobs. After a year, 80 percent of them either were still on the job or had gone on to better jobs. One in three had moved on to better housing; one in four had started night school or community college. Program officials gave the Journal method much of the credit and agreed with Progoff: "Poverty is not simply the lack of money. Ultimately, it is a person's lack of feeling for the reality of his own inner being."
                </p>

                <p>
                  Perhaps this is one reason for the popularity of Progoff's <em>Intensive Journal</em> method today: people feel poor and alone and devoid of ultimate meaning in their lives. In the Journal workshops, they have found a way to remedy that.
                </p>

                <p>
                  But how? I didn't quite understand until I had gone to a Journal workshop myself. Last spring, I spent a weekend with a disparate group of artists, teachers, housewives, and some college students at the Terros Center in Warwick, New York. Father Lewis Cox, a tall, placid New York Jesuit who is one of 95 consultants trained by Progoff and authorized to give the workshops, got us started at 8:00 P.M. on a Friday by passing out loose-leaf notebooks filled with blank, lined paper and a series of 21 colored dividers. He invited us to enter the interior worlds of our own memories and imaginations, opening our "exploration" by helping create some preliminary moments of meditative silence. Then he invited us to answer the question for ourselves, "Where are you now in your life?"
                </p>

                <p>
                  Father Cox said the answer might not, probably would not, come in the form of a judgment or as an answer in a college quiz. We might have an image—see a picture of ourselves on a bumpy plane ride or hear the strains of a favorite symphony. Whatever it was, the point was not to merely think about it. We were to write it down in a section of the Journal called the Period Log and were to refrain from making any judgments about whether the images we recorded were good or bad. It was all right, Father Cox said, soothingly and assuringly. We would return to it later.
                </p>

                <p>
                  After perhaps half an hour of work on the Period Log, Father Cox asked us to turn to some red sections in the middle of our books, first to the Life History Log, subtitled Recapitulations and Rememberings. He invited us to submerge ourselves in our own underground stream of recollection, but not to begin writing anything like an autobiography—just quick, significant scenes in our lives. Again, no judgment. Just get into that underground stream.
                </p>

                <p>
                  Soon we were into the next section of the Journal workbook, called Steppingstones. Here, Father Cox asked us to set down what could be chapter headings in an autobiography—not only an objective sequence of events, epitomized in a word or two or an image, but also a subjective perception of meaning and value. My Steppingstones turned out to be people; I wrote down a few dozen names—people I'd loved, people I had a hard time loving. Next, I wrote down the titles of the four books I'd published, the significant jobs I'd had.
                </p>

                <p>
                  And then it was time to retire until the next morning. So far, there had been hardly any conversation, hardly any noise. At one point, I had looked around the room and seen a few dozen heads bobbing over a few dozen notebooks, a few dozen ball-point pens gliding away. A lot of intensity and then, when the evening ended, a collective exhaling of breath and blinking of eyes. I thought I noticed tears streaming down the cheeks of an elderly man with a gray beard.
                </p>

                <p>

For some of the next day we amplified our Steppingstones section, going deeper into any period that seemed to draw our special interest. We made the same meditative trip in another section of the workbook, called Intersections, subtitled Roads Taken and Not Taken. I thought of my own spiritual intersections: I'd studied to be a priest for 10 years, then took another path. I thought of my emotional intersections: marriages, other loves. I thought of my career intersections: I'd worked for Time, then went free-lance and lived in a paradise in the High Sierra, then opted for a richer life of the mind by taking a job in New York with the Times.
                </p>

                <p>
                  And then Father Cox asked us to move to another section of the workbook, the Twilight Imagery Log. "We turn our attention inward, and we wait in stillness," he said, "and let ourselves observe the various forms of imagery that present themselves. We let them come of themselves. As they take shape, we perceive them. We observe them as though they were dreams. We describe them in the same neutral, non-interpretive, nonjudgmental way that we record our dreams."
                </p>

                <p>
                  Unbidden, the image of a roller coaster came to my mind, and I recorded the roller coaster of my life. I didn't dwell much on it there and then, but later I realized that that was what Progoff meant when he wrote that, frequently enough, images deep inside us "enable our life to disclose to us what its goals and its meanings are." For me, the image of the roller coaster was fun—and depressing: how can I ever manage to get off the roller coaster without breaking up in little pieces ?
                </p>

                <p>
                  For many of us, the most creative time was spent in the Dialogue Dimension, part of an entire system of Journal Feedback where, after laying out our life, we could not only step back and look at it but also explore its meanings. We did this, in part, by engaging in imaginary conversations with some of the significant people we'd already listed in the Steppingstones section.
                </p>

                <p>

To help make sure that the conversation wasn't one-sided and that we didn't give ourselves all the good lines, Father Cox suggested that we go through a short Steppingstones exercise for "the other." (I recalled an old Indian proverb I'd heard in my Arizona boyhood: "Judge no man until you've walked a mile in his moccasins." Father Cox's suggestion made sense.)
                </p>

                <p>
                  I did the Steppingstones for my daughter Polly, in England, who has been visiting me only in the summers since she was a tot, except for last summer, when she didn't come to the United States at all. I got into her moccasins, and this dialogue ensued:
                </p>

                <p>
Polly: You going to ignore me again? </p>

<p>  Me: Ignore you? </p>

<p>
  Pony: You did last time.
</p>

<p>

Me: I was in a different place last time. That was two years ago. I was broke, alone, insecure.
</p>

<p>
  Polly: And now?
</p>

<p>
  Me: I've got some money in my pocket, I've got friends, I'm secure. And I'm so happy you're coming this summer.
</p>

<p>
  Polly: You'll spend some time with me?
</p>

<p>
  Me: I’ll have to - or lose you.
</p>

<p>
  Polly: No. Not for that reason. Because you want to, because I am someone, too. I don't want you to love me because you have to. I want you to love me because you want to. I am a person, too, flesh of your flesh, but my own person, too. Look at me, listen to me, understand what I want.
</p>

<p>

Me: That's very hard. I get a lump in my throat.
</p>

<p>Polly: You feel.. . ?
</p>

<p>
  Me: I don't know. Guilty, maybe. Afraid that I won't come through for you ?
</p>

<p>
  Polly: I don't want anything from you. Just your undivided attention for a time.
</p>

<p>
  Me: Polly, you have to let me be me, too. I enjoy the roller coaster. If you want to be with me, you've got to get on the roller coaster, too.
</p>

<p>
  Polly: Okay. But don't forget I'm here, next to you.
</p>


  <p>
    At the end of this exercise, Father Cox suggested we might start to re-read what we had written, then write down how we felt. I wrote: "Sense of shame: I knew this all the time, but I wasn't paying attention. Enlightening. Shocking."
  </p>

  <p>
    There wore other dialogues: with society, with events, with the body, with works. The most productive dialogue was with the book I'd been doing on and off, mostly off, for two years.
  </p>

  <p>

Book: Help!
  </p>

  <p>
    Me: Hello.
  </p>

  <p>
    Book: Don't you recognize me?
  </p>

  <p>
    Me: Oh, yes, you're my memoir.
  </p>

  <p>
    Book: You're not paying me enough attention.
  </p>

  <p>
    Me: Strange, that's what the women in my life keep telling me.
  </p>

  <p>
    Book: You can't do everything.
  </p>

  <p>Me: I try.
  </p>

  <p>
    Book: At what cost?
  </p>

  <p>
    Me: Everything suffers. I suffer.
  </p>

  <p>
    Book: So?
  </p>

  <p>Me: So I guess I'd better set some priorities.
  </p>

  <p>
    Book: That sounds very old and very ...Jesuitical. What do you really want?
  </p>

  <p>
    Me: I want it all.
  </p>

  <p>
Book: You've got to conserve your energies for the most important things first.
  </p>

  <p>
    Me: The old "necessary, useful, agreeable" rule, huh?
  </p>

  <p>

Book: That's too puritanical. Have you ever thought about going with your feelings?
  </p>

  <p>
    Me: Sometimes it gets me in trouble.
  </p>

  <p>
    Book: Some people call that living.
  </p>

  <p>
    Me: Say, whose side arc you on, anyway? I thought you were complaining that I wasn't giving you enough attention.
  </p>

  <p>
    Book: I was, but if you're harried when you come to me, what good arc you? You're only going to be writing down stuff you end up throwing away.
  </p>

  <p>
    Me: So how do I arrange to come to you unharried?
  </p>

  <p>
    Book: Don't be taking on so many things, so many people.
  </p>

  <p>
    Me: Some call it living.
  </p>

  <p>
    Book: Touché! But can't you strike a happy balance? Get a rhythm going?
  </p>

  <p>
    Me: I got rhythm, I got music.
  </p>

  <p>
    Book: Buffoon. You're avoiding something by being a buffoon. What is it?
  </p>

  <p>
    Me: I don't know.
  </p>

  <p>
    Book: You afraid of something?
  </p>

  <p>
    Me: Maybe. Some of this stuff is pretty intimate.
  </p>

  <p>
    Book: That's what makes a good memoir. I love that.
  </p>

  <p>
    Me: But I just don't want to look like a fool.
  </p>

  <p>

Book: You want to write an honest memoir?
  </p>

  <p>
    Me: Yes.
  </p>


  <p>
    Book: Then maybe sometimes you have to risk looking like a fool—if you want to be honest.
  </p>

  <p>
    Me: An honest fool, huh? Some people may laugh at me.
  </p>

  <p>
    Book: Who?
  </p>

  <p>
    Me: Stupid people.
  </p>

  <p>
    Book: Good people?
  </p>

  <p>
    Me: No. They'll applaud me for taking chances.
  </p>

  <p>
    Book: Then why don't you?
  </p>

  <p>
    Me: Loosen up, huh?
  </p>

  <p>
    Book: You don't want me to sound staid and stuffy and boring, do you?
  </p>

  <p>
    Me: Nope.
  </p>

  <p>
    Book: Then let go.
  </p>

  <p>
    Me: Okay.
  </p>

  <p>
    For the record, although I did not make any "resolutions" at the Journal workshop, that dialogue helped point a way for me: I started working on my memoir, something I had been postponing for 10 years, and finished it just before Christmas. The book took about nine months to "emerge.'' I must have a dialogue with it soon. Or, at least, Soon.
  </p>

  <p>
    At the Journal weekend, there was hardly any interplay among the members of the workshop. We'd chat a bit at coffee breaks or at lunch, and that was it. There were none of the social pressures I'd experienced in any number of encounter groups, and therefore, no play acting was necessary. Furthermore, since I knew no one was going to see or hear what I was writing, I felt a sense of perfect freedom. Several times Father Cox drew a session to a close by issuing an open invitation to the group: would anyone care to read what he or she had just written? Some accepted his invitation, some didn't. It didn't seem to matter. Father Cox said that reading aloud was for the reader’s benefit, not the group's. Even so, I couldn't help feeling good about the feelings, often of joy, that were evident in the notebooks of others.
  </p>

  <p>
    One woman reported that she had kept a diary for 50 years and had "never listed a feeling or an awareness— just events as they happened." Now, under guidance, she said she had been able to write down her own feelings and felt exhilarated in the process. Moreover, she found new direction in her life: recently widowed, she simply didn't know what she'd do next. But in the Journal chapter called Intersections, she remembered a road she had very much wanted to take at one time—but had taken another that led to marriage and a family. Now, she realized, there was nothing stopping her from going back and taking the other road.
  </p>

  <p>
    After my own Journal workshop, I was all too aware that I'd only just begun to scratch the surface of the Journal process. There were a good many sections of the Journal that Father Cox hadn't even told us about. I found that I was in the first of three stages: a Life Context Workshop. I could go on to a Depth Contact Workshop and then, finally, to a Journal Feedback Workshop; in these, I would get a chance to work with, among other things, my own dreams.
  </p>

  <p>
    In brief, Progoff looks at dreams as most benevolent messengers, bringing things up from our own inner wisdom. "Dreams," he says, reach back into the past and call our attention to those experiences that can give us a clue with which to solve our present problems and move into, the future. Our dreams can give us these clues, however, in the only mode of functioning that is available to them—that is on the unconscious level, by indirection, allusion, imagery, and symbolism." In one of Progoff's theoretical works, Depth Psychology and Modern Man, he says: "Image-making is not a conscious process. We cannot create our images. The very reverse is true." In a way, then, we don't make our dreams; our dreams make us. Sounds intriguing, I said to myself. I have to go on with this.
  </p>

  <p>
    I thought I might attend further workshops. However, I also realized that I could work on alone and at my own pace, using Progoff's two major guides in the Journal method: At a Journal Workshop (now in its 12th printing), a basic introduction that would help me take my life in my own hands and draw it together, and The Practice of Process Meditation (just published) which would help me open up a whole new spiritual dimension. A warning: like the Spiritual Exercises of St. Ignatius Loyola, written more than 400 years ago, these Progoff works, though often original, are not so much books to read and understand as they are manuals of things to do. Do them, and then you understand better.
  </p>

  <p>
    Anaïs Nin, a diarist who logged an estimated 150,000 pages before her death in 1977, reviewed At a Journal Workshop in 1975 and noted that Progoff had found a way to help people toward intimacy, intimacy with themselves, intimacy with others. She then remarked:
  </p>

  <p>
    "The lack of intimacy with one's self, and consequently with others, is what created the loneliest ant most alienated people in the world Progoff ultimately proves that the process of growth in a human being, the process out of which a person emerges, is essentially an inward process."
  </p>

  <p>
    And where does that lead? Progoff’s answer is commonplace: it leads to meaning and to truth.
  </p>

  <p>
    But when you ask "Whose meaning? Whose truth?" Progoff answers: "Your own . . . To the reality of your own inner being."
  </p>

  <p>
    The reality of your inner being. If that is what Progoff is about, he is a braver man than his quiet, unassuming, professorial demeanor suggests. He tends to agree that he's not selling 1981's hottest product. "In Freud's Victorian age," he says, "the awful secret that nobody wanted to talk about was sex. Today, the awful secret is spirituality. People today will discuss anything but their inner life." The wonder is that Progoff has gotten thousands to start working on (if not actually talking about precisely that—and, moreover, in the hard-driving hurly-burly of the United States today, where men and women are lucky simply to keep the body alive, never mind the soul. Some sociologists of religion claim that a majority of Americans have rejected the very notion of spirituality as something pious and impractical and all-too-dependent on unreal dogmas committed to memory long ago by their local priests, ministers, and rabbis and handed on to the faithful in the form of slogans that were sappy and of categories that did not contain.
  </p>

  <p>
    Nevertheless, Progoff has gotten precisely those secularized Americans involved in a search for meaning. He's done it because, though he has a reverence and a respect for all the great thinkers and all the great religions, he has recognized that this is a time when autonomous men and women need to find their own meaning. "It is," says Progoff, "a difficult time, because the old answers don't respond to the new questions. It is also a time of opportunity, because now we have to work out new ways of dealing with ourselves, with others."
  </p>

  <p>
    In brief, Progoff seems to have secularized spirituality. How has he done it? His immediate answer is: "I don't do it. The people who come into the workshops do it—for themselves." He quotes Karl Rahner, the German Jesuit theologian: "The theological problem today is the art of drawing religion out of a man, not pumping it into him. The art is to help men become what they really are."
  </p>

  <p>
    Progoff, of course, quotes a good many men and women who come from traditions quite different from Rahner's. He is rather proud that his theory of human personality and human creativity has come from a long line of thinkers stretching back into history: Lao-tzu, Buddha, Augustine, St. Francis of Assisi, Feodor Dostoevsky, Jan Christiaan Smuts, Henri Bergson, Carl Gustav Jung, D. T. Suzuki, Martin Buber. The amazing thing is that this disparate bunch end up saying pretty much the same thing to Progoff. "I am not so original," he says. "I am a synthesizer."
  </p>

  <p>
    The synthesizer began life on August 2, 1921, in the Williamsburg section of Brooklyn, the son of a man who dropped out of the rabbinate to sell furs. One of Progoff's earliest recollections: his grandfather, an Orthodox rabbi, prostrate before the ark, rising to tell him: "You are the one who will do great things."
  </p>

  <p>
    He wondered for a good long time what the great things might be. "The ‘right answers’ that people gave me," he told me recently, "were usually superficial. I went to Brooklyn College in the late 1930's and early 1940's and found that the Marxism so popular there was superficial, too." But he had an economics professor, an Australian named Findlay MacKenzie, who "had a habit of putting mystical books in front of Jewish boys. He handed me a copy of Manly Palmer Hall's lectures on ancient philosophy, and that gave me the realization that history didn't start with the French Revolution. That the main things about history were not economic. And that they were mysteries."
  </p>

  <p>
    Four years in the U.S. Army were, for him, a dark night of the soul. "The only good thing that happened to me, " he says, "was that a chaplain gave me Israel Zangwill's Dreamers of the Ghetto. It brought together the tragedy and the pathos of Jews like Heine and Spinoza who were trying to enter the modern world. It introduced me to Ba'al Shem Tov, the founder of modern Jewish spirituality, Hasidism. And it brought me closer to my own Jewish roots."
  </p>

  <p>
    After the war, although he got "a European education" from a whole mob of brilliant expatriate professors who had fled Germany and taken up residence at the New School in New York, Progoff floundered academically. He did a dissertation on Jung that was "too long for a master's and not long enough for a doctorate." He got a job working as a welfare investigator on the Bowery, ''out of a sense of giving up."
  </p>

  <p>
    But he spent his evenings reading the novels of Balzac and the spiritual journals of Emanuel Swedenborg, "the Buddha of the North." He read Holism and Evolution by Jan Christiaan Smuts, who claimed that "psychology shouldn't deal with sick people, but with great people," and he read Thorstein Veblen on "the unfoldings of history and whether anything can be done about it." He read the poems of Walt Whitman who said yes to life and yes to the spirit in the midst of a brutal America. He read Lao-tzu, the first philosopher of Chinese Taoism, whose meditations on the cyclic in all earthly affairs rang true to him, and he read the essays of Henri Bergson, whose insight into the creative evolution taking place in Everyman, the elan vital, rang even truer.
  </p>

  <p>
    But it was also a period in Progoff's life in which he really didn't know where his vital force was leading him. He was tempted to cry out, almost in despair, "If God had wanted something great from me, He would have worked it out for me by now." Finally, he finished his thesis, Jung's Psychology and Its Social Meaning, and found on the strength of it, and to his surprise, that something called the Bollingen Foundation had given him a fellowship to go to Zurich and study under Carl Gustav Jung himself.
  </p>

  <p>
    He was with Jung from 1953 to 1955, met D.T. Suzuki, the great Zen master, at Jung's table, then returned to direct the Institute for Research in Depth Psychology at the graduate school of Drew University in New Jersey, spending a good deal of research time with his students on the great creative lives of all history. "At the time," Progoff recalls, "I was making the grand sum of $300 a month. To save money, I started writing my books on half-sheets of typing paper. My manuscript looked like a stack of toilet paper."
  </p>

  <p>
    The "toilet paper" was to become The Death and Rebirth of Psychology in 1956, and the first work in a trilogy of his own theoretical synthesis. Depth Psychology and Modern Man followed in 1959, and The Symbolic and the Real in 1963. At the same time, as a licensed psychologist, he started taking in patients.
  </p>

  <p>
    "People came. I had a large practice. They paid me. But I should have paid them. I was learning from them; I was beginning to see in them how a life unfolds. In terms of any social interest, I didn't know what that meant. But the Quakers soon showed me. In 1957, I was invited to spend a weekend at Haverford College at 'The Friends' Conference on Psychology and Religion.' Robert Greenleaf, one of the participants, an executive with AT&T, turned his life around after that meeting. He went on early retirement and opened up the most creative time of his life."
  </p>

  <p>
    Progoff was learning the impact of his holistic depth psychology on others. Soon he would start learning what impact it could have on himself. In the mid-1960's, he went through a profound emotional crisis: he and his wife separated, and it looked for a time as if he might lose his two children. What to do? He started noodling around in a notebook and discovered that he was able to get a fix on his life by writing things down, going over them, feeding them back into the computer of his mind, as it were, engaging in a dialogue with himself and with his wife and children.
  </p>

  <p>
    "I could see that I was onto something, but I wondered how original the method was. At the Quaker Library at Pendle Hill, I found (and read) Quaker journals of the 17th and I 8th century. As far as I could see, the Quakers used their journals as a way of keeping tabs on their consciences (after they'd thrown away ritualized confessions). For them, it was only a means of self-measurement—against which they inevitably failed.
  </p>

  <p>
    "I looked at other journals, notably those of Dostoevsky and Anaïs Nin, and I could see that, for them, the journal was a vehicle that led to greater creativity. But I found that a good many other journals were just diaries: without a project to be done, people's diaries just went around in circles."
  </p>

  <p>
    Without a project -that was the key that opened the door for Progoff. He'd already seen in his own case that a journal could lead somewhere for him if he had a problem. It could also lead somewhere for a novelist or a writer with a project. But what about everyone else in the world? What projects do they have? The answer came to Progoff in a flash, the result of all his previous reading, from Lao-tzu to Smuts to Buber: everyone has a life and that life must be his or her great work of art. The synthesis had produced something new, the <em>Intensive Journal</em> system, aimed at helping almost anyone who wished to start thinking of life as a work of art, of becoming, in Rahner's words, "what they really are."
  </p>

  <p>
    A Thomist philosopher would explain the process, in part, by citing the notions of Aristotle and Aquinas on potency and act, final and efficient causality. You plant an acorn and you get an oak. You plant a tomato seed and get a tomato plant. But what do you get when you plant a human seed? Nothing so identical as oaks to oaks or tomatoes to tomatoes. In what direction does a human life go? Says Progoff: "We’re limited, in part, by our own culture. Gautama Buddha couldn't become a Francis of Assisi. But aside from that, we all have free will. We can become pretty much what we want to."
  </p>

  <p>

But why do some people go only so far, and others much further? Ah, that’s Progoff's next book, the work he began years ago at Drew, the study of creative lives.
  </p>

  <p>

"I'm 59 years old," Progoff says, with a smile. "As a good Jewish boy, l have to believe I'm going to live to be about 120. So I figure I'm just about to the halfway point in my life, the point where Jung says we begin our most serious work.... Now, what I'd like to do is get the most creative men and women in the world and give them Journals. . .''
  </p>

  <p>
    Robert Blair Kaiser is a reporter for the New York Times.
