                  <h1>Enhancing the EfM Experience: an interview with The Rev Richard E Brewer* </h1>
                <em>
                  <p>by Jon Progoff**</p>
                </em>
                <em>
                  <p>by Jon Progoff** The Rev Richard E Brewer explains his long-time involvement in the <em>Intensive Journal</em>® program and discusses whether it can be an effective tool for participants in the Education for Ministry (EfM) program.</p>
                </em>
<h6>
  A.  Your Involvement in EfM and the <em>Intensive Journal</em> Program
</h6>

<span>
                    <strong>
                    JP: You have been involved in building EfM since the early years.  Tell us about how you have served.
                    </strong></span>
                    <br>
<p>
  <strong>
    RB:</strong>
  My first contact with EfM was in its very earliest days of development.  In 1975, I met with Dr. Charles Winters, founder and director of the program, and in 1975 I started my first group in Stillwater, Oklahoma. Over the years, I have served as mentor, trainer, trainer of trainers, developer and author of Common Lessons, primary author of the study guides called Reading and Reflection Guide (RRG), co-author with Angela Hock of Practically Christian (a fifth year offering), and I served as Interim Assistant Director of EfM for a year.
  <br class="u-margin-bottom-small-1">
  My earliest years in the program included working with Flower Ross, who gathered the first trainers and led us to develop the model and method of theological reflection.  I have trained mentors across the country since 1979, and continue as a trainer and mentor.
 </p>

<span>
                    <strong>
                    JP: What special qualities of EfM make it a unique program in religious education?
                    </strong></span>
                    <br>
<p>
  <strong>
    RB:</strong>
  Several qualities make EfM unique. First, it is grounded in the participants’ experience. While EfM participants increase their knowledge of the content of the Judeo-Christian traditions, they learn how to integrate that content into the context of their lives. Second, the program is firmly based on adult experiential education theory and practice, which results in participants taking the primary responsibility for their learning. A third quality contributing to its uniqueness is that the program’s primary learning is located “in the field” where those involved stay firmly immersed in their day-to-day lives.
  <br class="u-margin-bottom-small-1">
  EfM from the beginning integrates insights and learning from the field into the methods and models of the program. That feedback standard has kept the program fresh, up-to-date, and responsive to the participants’ lives while engaging them in current theological trends. The requirement of on-going training of the mentors adds a fourth uniqueness that allows the program to include the most current advances in theology and have a consistent channel for mentors to participate in the continual development of the program. Lastly, through the practice of theological reflection, participants learn how to view their world through theological lenses and understand their daily lives as the context for their Christian ministry.
 </p>

<span>
                    <strong>
                    JP: You have also been involved in the <em>Intensive Journal</em> program for many years, for a time one of its certified instructors.  What attracted you to the <em>Intensive Journal</em> program?  How has it helped you?
                    </strong></span>
                    <br>
<p>
  <strong>
    RB:</strong>
  In the mid-1970s, I traveled to New York and attended a series of workshops that Ira Progoff, PhD, led at his office at Dialogue House. The use of the spiritual dimension of the workbook (now called the Meaning Dimension)  solidified my involvement with the <em>Intensive Journal</em> process. Over the next ten or more years, I attended a week-long <em>Intensive Journal</em> retreat each summer at various locations across the country.   As I became more at home with the use of the <em>Intensive Journal</em> method, I found it to be an essential part of my spiritual growth.
  <br class="u-margin-bottom-small-1">
The <em>Intensive Journal</em> work supported my personal and professional life. I was in my formative years as an Episcopal priest. The approach to life that the <em>Intensive Journal</em> method advocates gave me the structured support I needed as I sought to make meaning of my experiences. The principle of working within the context of my whole life in a progressively deepening process shaped my way of being in the world and my work as a trainer, mentor, and curriculum designer.  It became integrated in how I live, move, and have my being.
 </p>

<span>
                    <strong>
                    JP: How does the <em>Intensive Journal</em> process further your goal of living your life as a Christian?
                    </strong></span>
                    <br>
<p>
  <strong>
    RB:</strong>
I sought to be an <em>Intensive Journal</em> instructor when I realized that the <em>Intensive Journal</em> work “fit” with and was pertinent to the theology as mediated through the Episcopal Church and the work of Christianity. I concluded that the <em>Intensive Journal</em> work provides in-depth understanding of the incarnating process of the holy. The <em>Intensive Journal</em> sections provide channels through which the waters of one’s life could flow, in turn opening one to the full breadth and depth of human consciousness. In short, the work supports the creation of an experientially-based theological anthropology. Additionally, the understanding of life as a dynamic evolution formed my awareness of life as an ongoing process. In the terminology of Christian theology, life is a continual incarnating process through which one moves toward unity with God.
  <br class="u-margin-bottom-small-1">
William Porcher DuBose, an influential early 20th century Episcopal theologian, spoke of the double incarnation:
  <br class="u-margin-bottom-small-1">
  <span class="u-center-text u-italicize-text">
    . . . the great truth that the Gospel of God in its entirety is not a single but a double incarnation: it is not only God’s Word of Truth manifested to us objectively in the flesh of Jesus Christ; it is also God’s Spirit of Life manifested in us subjectively in our own flesh, which means our own minds and hearts and lives. God in Christ is only half the truth and the mystery of the Incarnation; Christ in us is the full other half.
  </span>
  <br class="u-margin-bottom-small-1">
The <em>Intensive Journal</em> method provides access to the full dimensions of human experience. I understand the subjective work done through the <em>Intensive Journal</em> process as the work of realizing the second half of the double incarnation.
  <br class="u-margin-bottom-small-1">
In short, the <em>Intensive Journal</em> process has helped me personally by providing a way to do the intrapersonal work of the incarnating process. Professionally, it has provided me with an experiential base from which to profess my understanding of Christian life and work.  Out of what I experienced, I better understand my religious tradition and those in it who have gone before me.
 </p>

 <h6>
   B.  Using the <em>Intensive Journal</em> Method to Enhance the EfM Experience
 </h6>


 <span>
                     <strong>
                     JP: EfM has five thematic areas that are at the core of the curriculum for each of the four years.  Let’s start with the first theme:  developing your spiritual history and enhancing your listening skills.  Does the <em>Intensive Journal</em> method help further this thematic goal? If so, how?
                     </strong></span>
                     <br>
 <p>
   <strong>
     RB:</strong>
The spiritual autobiographic work in EfM has two purposes. First, participants need to have access to their life story, which is primarily an intrapersonal experience. Second, sharing part of the story within the seminar contributes to the establishment of a learning community. The <em>Intensive Journal</em> method supports primarily the inner personal dimension of the autobiography work, providing a systematic way to create a comprehensive expression of one’s life history.  The Steppingstone exercise of the <em>Intensive Journal</em> method has been directly built into one of the approaches EfM uses for constructing one’s spiritual autobiography.
   <br class="u-margin-bottom-small-1">
 Listening requires a quieting of one’s mind which can be done with quieting one’s “mind talk.” The <em>Intensive Journal</em> method uses a neutral form of stillness meditation (called Entrance MeditationTM  readings) to help one settle into a centered place from which one can empathetically listen to another’s story. A participant who has attended <em>Intensive Journal</em> workshops could use its structure to gather what is stirring in one’s life in response to hearing others’ stories. I envision this as something done on one’s own after the EfM seminar.
  </p>


 <span>
                     <strong>
                     JP: The second thematic area is fostering theological reflection.  Describe the process of theological reflection and how the <em>Intensive Journal</em> method can be used to further that process.
                     </strong></span>
                     <br>
 <p>
   <strong>
     RB:</strong>
     Theological reflection describes a conversational process built on a Four-Source Model involving content from various sources such as:  the Judeo-Christian heritage (the Tradition Source); social and cultural perspectives,
     attitudes, and material (the Cultural Source); an individual’s subjective experience (the Action Source); and an individual’s beliefs, opinions, and convictions (the Position Source).
   <br class="u-margin-bottom-small-1">
   EfM uses theological reflection as a primary method for participants to know wisdom, truth, insight, understanding, and meaning as contained in the Judeo-Christian traditions. The <em>Intensive Journal</em> program shares a common interest in such but mainly through the individual’s subjective experience. Both EfM and the <em>Intensive Journal</em> method hold the individual’s experience as primary, but their way into wisdom is very different.
   <br class="u-margin-bottom-small-1">
   One way in which work within the <em>Intensive Journal</em> method can foster theological reflection is by extending the experience of theological reflection. The structure of the <em>Intensive Journal</em> method provides a systematic way to extend the insights experienced within a theological reflection into other areas of one’s life.
   <br class="u-margin-bottom-small-1">
 For example, if one has a clearer image of an issue at hand, then the insight might be extended through asking a key question that is used in the <em>Intensive Journal</em> method, “Where else in my life does that learning lead me?” Then, one can follow the <em>Intensive Journal</em> procedures to build one’s theology of life. In short, the <em>Intensive Journal</em> process offers a way for an individual to take the results of the EfM theological reflection process and provide a means so that the awareness becomes a part of one’s ongoing journey into meaning and truth.
  </p>

 <span>
                     <strong>
                     JP: Constructing a spiritual development plan to support a ministry agenda is the third thematic area. Summarize the focus of this area and describe how participants can use the <em>Intensive Journal</em> method.
                     </strong></span>
                     <br>
 <p>
   <strong>
     RB:</strong>
The EfM spiritual formation work is contained mostly in the third unit of the study guides (RRG).   One goal of the work is for participants to develop spiritual practices that sustain their ministry. The RRG guides them into practices such as Examen, Ignatian meditation, centering prayer, and use of the labyrinth. The <em>Intensive Journal</em> process helps integrate the spiritual practices into daily life and can be included as an additional practice. The spiritual related exercises of the <em>Intensive Journal</em> process provide guidance and support for the spiritual formation facilitated in the EfM program.
  </p>

 <span>
                     <strong>
                     JP: The fourth thematic area is examining the relationship between belief, behavior and doctrine. Describe some ways that the <em>Intensive Journal</em> method can help someone develop insights in this area.
                     </strong></span>
                     <br>
 <p>
   <strong>
     RB:</strong>
The relationships among belief, behavior, and doctrine (teachings) are examined so that one’s theology is more coherent and congruent. The integration of belief, behavior, and doctrine results in bringing theory and practice together to create an integrated person. The integration process operates within every dimension of a person; one’s thoughts, feelings, and actions drawn together move one into wisdom and meaning.
<br class="u-margin-bottom-small-1">
The <em>Intensive Journal</em> process provides structure and procedures that move one into wholeness in a safe and natural way. Individual work that the <em>Intensive Journal</em> method facilitates allows a person to accomplish the integration in one’s own timing and readiness. The <em>Intensive Journal</em> procedures support  the integration process. EfM participants who have attended the <em>Intensive Journal</em> series can deepen their work in the relevant exercises to further their work in the integration between belief, behavior and doctrine.
  </p>

 <span>
                     <strong>
                     JP: Exploring ways to listen to and respond to God’s call to ministry is the fifth thematic area.  How can the <em>Intensive Journal</em> method help achieve this process?
                     </strong></span>
                     <br>
 <p>
   <strong>
     RB:</strong>
Vocational issues are attended to throughout the EfM year and are addressed directly in the final unit of the Reading and Reflection Guide. The primary understanding of vocation is not career, but how to love authentically as a Christian in daily life. The formation of one’s vocational sense develops incrementally through the weekly theological reflection work. In short, the purpose (telos) of EfM is measured by the wisdom one draws from the Christian tradition to live more fully from day to day.
<br class="u-margin-bottom-small-1">
The <em>Intensive Journal</em> method supports the overall vocational purpose in three ways. First, the <em>Intensive Journal</em> method and procedures provide a way for individuals to integrate the four years’ work by attending an <em>Intensive Journal</em> retreat. Second, if participants attend <em>Intensive Journal</em> workshops while in EfM, they can then actively use the method while participating in EfM to build their inner life within the context of learning the overall arc of the Judeo-Christian heritage. Third, the <em>Intensive Journal</em> method provides guidance for the participants’ continual growth after EfM has been completed.
  </p>

 <span>
                     <strong>
                     JP: What are some of the major attributes of the <em>Intensive Journal</em> method that EfM participants will find useful? Is there a congruence between the two programs? If so, how?
                     </strong></span>
                     <br>
 <p>
   <strong>
     RB:</strong>
The <em>Intensive Journal</em> method offers freedom to do the inner work at one’s own pace and timing. It does not require ascent to any doctrine or belief. EfM presents participants with a comprehensive scope of Christian teachings without requiring assent to any of them. As the founder of EfM once said, “I don’t care if one becomes a heretic, but I do care that one recognizes whenever herectical positions are being held.” EfM supports the individual in creating theological meaning relevant to the context of one’s life.
<br class="u-margin-bottom-small-1">
Several congruencies exist between the two programs. Both seek to create a supportive atmosphere in which one can grow into meaning and wisdom. The two programs require a trained leader to guide participants and to protect the integrity of the program. Life is a continual process of growth that is lifelong. Both programs are designed to provide ways in which the growth continues after the formal training has been completed. The primary congruence of the two programs is they both provide ways for living life with greater wisdom.
  </p>

  <h6>
    C.  Continuing Education Opportunities and Concluding Remarks
  </h6>

   <span>
                       <strong>
                       JP: EfM has approved <em>Intensive Journal</em> workshops as an Alternate Mentor Training for EfM mentors, subject to certain conditions.  Summarize the benefits that  can accrue to mentors.
                       </strong></span>
                       <br>
   <p>
     <strong>
       RB:</strong>
I can best summarize the benefits accrued to mentors who attend the <em>Intensive Journal</em> program by speaking to how I as an EfM mentor have benefitted. The <em>Intensive Journal</em> method gave me access to a comprehensive experience of human nature. The dimensions of experience that the <em>Intensive Journal</em> process awakened led directly into developing a coherent theological anthropology that I draw from in facilitating conversations in the EfM seminars. <em>Intensive Journal</em> exercises in the deeper-than-conscious and the spiritual dimension expanded my experience in ways that helped me support others in their theological reflection work. The <em>Intensive Journal</em> process provides a safe place to explore in depth by working within the context of one’s life in a progressively deepening way. The same principle holds true for theological work done within an EfM seminar
  <br class="u-margin-bottom-small-1">
  Work in EfM as a mentor gives rise to a variety of conflicts. Difficulties emerge between participants; with the EfM methods; and with the program center. The <em>Intensive Journal</em>  method provides ways for mentors to clarify their inner response to tensions and conflicts before entering conversations with the conflicted persons. Such inner work contributes to spiritual growth with creativity and wisdom, enhancing a mentor’s effectiveness.
    </p>
   <span>
                       <strong>
                       JP: We have spoken about using the <em>Intensive Journal</em> method while enrolled in EfM.  There are thousands of graduates from EfM over its 40 year history.  Can they benefit as well? If so, how?
                       </strong></span>
                       <br>
   <p>
     <strong>
       RB:</strong>
EfM graduates can benefit from the <em>Intensive Journal</em> process signficantly; the key that opens graduates to the <em>Intensive Journal</em> program is to see that the <em>Intensive Journal</em> process offers a way to integrate further one’s beliefs and behavior in dialogue with Christian teachings. There are many exercises that can help guide inner integration of beliefs, behavior, and doctrine.
  <br class="u-margin-bottom-small-1">
<em>Intensive Journal</em> work builds one’s spiritual muscles to live more authentically as a person of faith in today’s world.   EfM primarily builds one’s theology within a community, and the <em>Intensive Journal</em> method encourages individual work that develops inner wisdom and strength.  EfM graduates have significant experience with spiritual practices and can view the <em>Intensive Journal</em> method as a multifaceted spiritual practice.
    </p>

   <span>
                       <strong>
                       JP: You seem to believe that the goals, philosophy, and approaches of EfM and the <em>Intensive Journal</em> method are congruent and the <em>Intensive Journal</em> program can play an important role in enhancing the EfM experience. If, so, what is your hope for these programs working together in the future?
                       </strong></span>
                       <br>
   <p>
     <strong>
       RB:</strong>
I can envision the two programs working in tandem with one another.  I see mentors using their <em>Intensive Journal</em> workbook as a personal resource as they guide participants through the EfM ministry formation process. EfM groups could host <em>Intensive Journal</em> workshops for their members, which would then open possibilities for additional entries at various points within the EfM program.  I think other opportunities will reveal themselves as the <em>Intensive Journal</em> program becomes better known within the EfM community.
    </p>


