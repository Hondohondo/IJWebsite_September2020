                  <h1>The <em>Intensive Journal</em> Program:<br>
A Spiritual Tool for Modern Times </h1>
                <em>
                  <p>by Rev. Wallace (Wally) Chappell</p>
                </em>

            <p>

The <em>Intensive Journal</em> process has been, for the years, one of the primary “tools” for my own inner journey in a life of ministry.
            </p>

                <p>
              Most of the great spiritual writers across the centuries stress being silent, listening, and attending to the voice of God. But the fact of life for busy pastors is the incessant schedule pressure which almost excludes quiet moments. It is hard enough to settle into silence, and even harder to linger there until God “speaks.” Our personal depths become as churned up as our life schedules. The result is very little serenity, meditation, or sense of God’s presence in what was intended to be a religious life.
                </p>

                <p>
        Within this condition of constant demands and endless doings, it is great to discover a modern system for achieving the deeper listening that brings a sense of God’s presence, peace, and will. The <em>Intensive Journal</em> program has been that for me. It gives us something to do, on the way to our deeper self. That doing, that deeper awareness, and the power for Godly living that flows from it, is a practical way of speaking of the <em>Intensive Journal</em> method. The method is proven by the results. The <em>Intensive Journal</em> process “works” for religious people who don’t live in a monastery.
                </p>

                <p>
        Details are too extensive for this testimony, but the result is beyond question:
                </p>

              <p>
                <ul>
                  <li>Current issues clarify themselves; </li>
                  <li>Old relationships are healed;  </li>
                  <li>Next steps define themselves; </li>
                  <li>Old agonies are dealt with; </li>
                  <li>A deep energy flows into life; </li>
                  <li>The “voice” of God stirs in our thoughts. </li>
                </ul>
              </p>

              <p>
                And this takes place at an experiential level, a level that underlies the intellectual structure of creeds and doctrines. You trust what you “hear.”
              </p>

              <p>
                Laity find the method powerful, not only in reconciling a troublesome past, but also in the discernment of next steps in life. In the language of classic spirituality, the <em>Intensive Journal</em> method is a work of discernment. Many also discover an “inner counselor” that leads them into a capability for living life without frequent pastoral ‘hand holding.’ One middle-age woman was ecstatic and thrilled to put to rest old issues with her father. She said, “It was like he was here talking to me.”
              </p>

              <p>
                The <em>Intensive Journal</em> program has been one of the foremost “pieces” of my own inner life for so long I wonder what I did before I found it. Of all the spiritual disciplines that I have used across the years, this one has been the most dependable in keeping me on track, energized, and focused. I recommend it to all pastors and the congregations they serve.
              </p>


