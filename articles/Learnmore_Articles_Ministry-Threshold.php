                  <h1>Review for Religious<br>
At the Threshold of a Christian Spirituality: Ira Progoff's <em>Intensive Journal</em>® Method</h1>
                <em>
                  <p>by John McMurry, S.S.</p>
                </em>

                <p>
              Since 1978 I have been teaching Ira Progoff s <em>Intensive Journal</em> method occasionally at weekend workshops. Dialogue House, the umbrella organization covering all of Progoff’s works, describes his method as a program of “professional and personal growth with a spiritual point of view.” It is a nonanalytic means for individuals to attain two goals. First, it enables individuals to recognize and accept the wholeness of their life without denying the reality of any of its contents, no matter how unpleasant or embarrassing. Secondly, it enables individuals to get a feel for the consistency in the direction that their life is taking as they discover potentials for the future hidden within their personal past.
                </p>

                <p>
          The goals of the program are attained by means of a variety of written exercises which are done in a group setting under the direction of an experienced leader who is committed to follow authorized guidelines. Individuals in the group work in private with the contents of their own life. The only prerequisites are an atmosphere of quiet and mutual respect, and an attitude of openness and acceptance on the part of each exercitant toward his or her own life.
                </p>

                <p>
              The program is not only nonanalytic; it is also nonjudgmental and is structured to help people exponentially discover answers to questions such as the following:
                </p>

                <p>
              Where am I in the course of my life right now? How did I get to the place where I am in the course of life? Where is my life trying to go from here? What is the next step?
                </p>

                <p>
          The <em>Intensive Journal</em> method itself has no content. The method is a dynamic structure to which each person supplies the content from one's own life. The structure aims at enabling individuals to establish deeper contact with the flow of creative energy in their own life. It is especially useful for people engaged in decision-making, for people who feel confused about the next step in life, for those who have lost contact with the direction their life wants to take, for those who feel alienated, isolated, or meaningless, and for those who simply want to expand their personal horizons of creativity.
                </p>

                <p>
              In creating the <em>Intensive Journal</em> program, Progoff had in mind people in a secular culture who are unfamiliar with or alienated by traditional religious language. However, the awarenesses stimulated by the exercises of his method serve to help Christians experience meaning in traditional doctrines which might otherwise remain merely propositional. In the case of people who approach it from the perspective of faith, the <em>Intensive Journal</em> program is a form of prayer.
                </p>

                <h6>
                  The <em>Intensive Journal</em> Method as Prayer
                </h6>

                <p>
            In a chapter entitled “Prayer as Dialogue,” Karl Rahner discusses prayer in terms apropos of the <em>Intensive Journal</em> method. He is addressing a common problem of people who are earnest in their efforts to enter into dialogue with God. They often state the problem something like this: "When I pray, I cannot tell whether I am talking to myself or to God."
                </p>


                <p>
            Rahner challenges the presupposition that God says "something" to us in prayer. He raises some “what-ifs”: What if we were to say that in prayer we experience ourselves as the utterance of God, ourselves as arising from and decreed by God's freedom in the concreteness of our existence? What if what God primarily says to us is ourselves in the facticity of our past and present and in the freedom of our future?
                </p>

                <p>
            Rahner concludes that when, by grace, we experience ourselves as the utterance of God to himself and understand this as our true essence, which includes the free grace of God's self-communication, and when in prayer we freely accept our existence as the word of God in which God promises himself to us with his word, then our prayer is already dialogic, an exchange with God. Then we hear ourself as God's address. We do not hear "something" in addition to ourself as the one already presupposed in our dead facticity, but we hear ourself as the self-promised word in which God sets up a listener and to which God speaks himself as an answer.'
                </p>

                <p>
              Rahner is suggesting that God's word to me in prayer is not an idea; rather, God's word to me in prayer is myself, that is, my personal, individual life story-past, present, and future. The implication is that my life story is important in my relationship with God because it is the way God speaks to me and I to God.
                </p>

                <p>
              A further implication of Rahner's proposal is that I enter into dialogue with God ipso facto under three conditions: 1) when I experience my life story as God's word addressed to himself; 2) when at the same time I understand that God is really present in my actual life story-past, present, and future- as a free and undeserved gift of himself to me; 3) and when I freely accept my life story as the word of God in which God promises his Word to me.
                </p>

                <p>
            The <em>Intensive Journal</em> program is an instrument which lends itself to the discovery of the real presence of God in one's own personal life story. The content of the program is the content of the life of the Journal-writer; hence it is through the life of the Journal-writer that Christian faith may enter into the individual's use of the <em>Intensive Journal</em> exercises. Progoff has described the prayer dimension of his method as follows:
                </p>

                <p>
The <em>Intensive Journal</em> work is indeed a species of prayer and meditation, but not in isolation from life and not in contrast to active life involvement. Rather, it is meditation in the midst of the actuality of our life experiences. It draws upon the actualities of life for new awarenesses, and it feeds these back into the movement of each life as a whole.<sup>2</sup>
                </p>


<h6>
  The <em>Intensive Journal</em> Method and Spirituality
</h6>
                <p>
In his “handbook of contemporary spirituality,” Rahner raises the question whether the term “spirituality” is good, understandable, useful, or even has any meaning. Then he makes the observation that the crucial point for personal and pastoral life today is not so much a matter of getting the “spiritual” dimension of existence into our heads or other people's by means of abstract and conceptual indoctrination (which he says is ineffective anyway) as it is a matter of discovering the Spirit as that which we really experience in ourselves. <sup>3</sup>
                </p>

                <p>
          Perhaps Rahner slightly understates the case. It may be that the crucial point for us personally and in our pastoral work today is simply to discover "the Spirit" as a fact of our own personal experience and to help others do the same.
                </p>

                <p>
                Furthermore, in order to be able to use the word “spirituality,” we might let it refer simply to the individual's relationship with God or, in other words, to what goes on in the creative process between God and each of us.
                </p>

                <p>
              This article presents Ira Progoff s <em>Intensive Journal</em> program as an aid to the process which is going on between an individual and God. The program adds no content to the life of the individual; it mirrors the movement which is already going on and stimulates that movement by feeding new awarenesses back into the movement of life. (“Journal feedback” is one of the main features which distinguish this method from an ordinary diary.) This program, then, is a dynamic structure for evoking self-transcendence from the factual contents of a life story. For a person of faith it is a way of discovering the Spirit “as what we really experience in ourselves.”
                </p>

                <h6>
                  Genesis of the <em>Intensive Journal</em> Method
                </h6>

                <p>
              Following Progoff s discharge from the U.S. Army, he earned a doctorate in the area of the history of ideas from the New School of Social Research in New York City. On the basis of his dissertation, Jung 's Psychology and Its Social Meaning, published in 1953 and still in print, Progoff was awarded grants for postdoctoral studies with Carl Jung for two years. By virtue of those studies Progoff was licensed as a therapist by the state of New York, where he went into private practice after returning from Switzerland.
                </p>

                <p>
        In 1959 Progoff founded the Institute for Research in Depth Psychology at Drew University in New Jersey and served as its director until 1971. During those twelve years he and his graduate students searched out the dynamics of creativity in published biographies of creative people whose life stories had ended. From his research Progoff concluded that creativity occurs through the interplay among various dimensions of life which may at first seem disparate. On the surface it may appear that the process in one dimension is unrelated to the process in another dimension, whereas in fact something new comes into being when the individual makes correlations among the dimensions of life. It is as though the individual is a complexus of certain processes which occur throughout life on different planes.
                </p>

                <p>
                Progoff has developed the <em>Intensive Journal</em> method over more than a quarter-century of helping his clients apply the fruits of his research by discovering hidden sources of creativity within their own lives. He began teaching his method to groups in the late 1950s. In 1975 he published At a Journal Workshop, a thorough description of his method up to that time. In 1980 he published a companion volume, The Practice of Process Meditation, which added another dimension to the program.
                </p>

                <h6>
                  Dimensions of Human Existence
                </h6>

                <p>
                  In Progoff s view, the artist is paradigmatic. Each individual is both the artist and the ultimate artwork of life, and yet individuals execute the artwork which, is themselves by engaging in outer activity which has inner meaning for the one doing it and beneficial consequences for society. In other words, in order for each of us to be fulfilled as an individual, we must do some work (opus as distinguished from labor) which is both personally and socially meaningful. At the same time as we are creating our lifework, the doing of the work is creative of us. The basic dialogue of life is the dynamic actual (as distinguished from logical) dialogue between human creators and their works. In Progoff’s words, “Outward activity propelled from within is the essence of creative existence.”<sup>4</sup>
                </p>

                <p>
                From his research on the lives of creative people Progoff learned to distinguish certain dimensions of life as loci of the components of creativity. The <em>Intensive Journal</em> method recognizes those dimensions as sources of the raw material of creativity in anybody's life. They are the dimensions of time, of relationships, and of personal symbols.
                </p>

                <p>
                  The <em>Intensive Journal</em> workbook uses color-coded dividers to mark off various sections in each of which the Journal-writer deals with the movement in one particular dimension of life. Within each of the main sections are tabbed subdividers of the same color as the main divider. Each tab bears the name of the specific exercise to be entered there. For example, the “Life/Time Dimension” is indicated by a red divider and contains four tabbed red subdividers; each of the four tabs bears the title of the written exercise to be entered there by the Journal writer. Similarly, the dimension of personal relationships in life, called the “Dialogue Dimension,” is indicated by an orange divider and comprises five tabbed subdividers for each of the five “dialogue exercises.” The part of the <em>Intensive Journal</em> workbook for making entries which deal with dreams and personal imagery is called the “Depth Dimension.” It is indicated by a blue divider and five tabbed blue subdividers.
                </p>

                <p>
                  In summary, the workbook comprises sections which reflect and stimulate the movement of an individual life in each of its dimensions. Each of the main sections of the workbook represents a dimension of life and comprises several subsections for various written exercises which deal with the contents of that life in styles appropriate to that particular dimension.
                </p>

                <h6>
                  The Dimension of Life/Time
                </h6>

                <p>
                  We do not get the chance to start life over, but the <em>Intensive Journal</em> program does offer us a tested means of restructuring our life from the perspective of the present. At the same time it provides a means of discovering unactualized potentials which we may have overlooked the first time around, or which were not ripe then and may at some point in time be able to take a form they could not have taken originally.
                </p>



<h6>
  A Tool for the Ministry
</h6>
                <p>
In studying the biography of a deceased person generally recognized as creative, the end or goal of that career may be clear and unmistakable, even though the life story includes setbacks, stalls, reversals, and obstacles. It may be easy to see how everything in that life was leading up to some great scientific or philosophical work because we are viewing it from the perspective of the end.
                </p>

                <p>
                But what if I am the life story I am working with? In that case the life process is still in progress. I am not looking at a still photograph but a moving picture, and I am looking at it from the inside. In that case I start with the present epoch of my personal life and get a feel for this period of life from the inside. That is, I allow myself to feel the quality or tone of my life during this present period and record it objectively. The record I make of the present period will be an objective statement of my subjective experience of life at present. Then I am in a position to allow the course of my life to present itself to me from the perspective of the present in the form of about a dozen significant events. Each of those significant events serves to characterize a whole period of life. Of course, many other things also happened during that period. There are other exercises for dealing with them. The idea in this exercise is to get a feel for the wholeness and continuity of my life as I allow it to present itself to me in an articulated form so that I can use other Journal exercises to deal with it one period at a time.
                </p>

                <p>
                  All the <em>Intensive Journal</em> exercises presuppose the attitude of openness and receptivity mentioned above, a nonjudgmental attitude toward life. It is not so much the objective contents of a life that affect its degree of creativity, as the subjective attitude toward that life. In the creative restructuring of a life, a relaxed, friendly approach which allows surprises is important.
                </p>

                <h6>
                  Dimension of Relationships
                </h6>

                <p>
                In the life/time dimension treated above, there is a principle of wholeness, continuity, and direction-toward-a-goal at work. In the dimension of relationships, the dynamic is that of dialogue, that is, the give-and-take of equals listening and responding to each other in a spirit of mutual trust and acceptance.
                </p>

                <p>
                  The principle of “dialogue relationship” applies first of all to significant people during various epochs of life. The same dynamic applies analogously to meaningful work-projects (opera), which, like persons, seem to have a life of their own.
                </p>

                <p>
                  In his research on creative lives, Progoff discovered that creativity occurs when people approach several kinds of meaningful contents of their life not as inert matter to be manipulated but as personal entities. That is, he discovered that creativity occurs when people acknowledge that each of several meaningful contents of their life has a life story of its own analogous to that of a person. Each of these contents of life has a life story with blockages to growth toward a goal, with hopes, disappointments, successes, and so forth. He found that for the sake of movement toward acceptance of life and all it holds, it is of paramount importance to establish a “dialogue relationship” not only with persons and works but with the physical and societal dimensions of life, and with events, situations, and circumstances of life which “just happen.”
                </p>

                <p>
                  Progoff’s research into de facto creative lives yielded two important corollaries. First, the movement which the dialogue relationship fosters is intrinsic to the creative spirit. Secondly, in the dimension of relationships as well as in other dimensions of life, the factual contents of life are less important in the creative process than the way people relate to whatever the contents of their life are.
                </p>

                <p>
                  The “Dialogue Dimension” of the <em>Intensive Journal</em> workbook offers a format for a variety of exercises which enable the Journal writer to engage in written dialogue with people who have played meaningful roles in their life, with work projects, their own body, sources of values in their life (e.g., family, ethnicity, religious commitment), and things over which they had no control. The purpose of these dialogue scripts is to give a voice to the meaningful contents of life, that is, to provide them a forum in which mutuality can flourish in the form of a "dialogue relationship" rather than a merely utilitarian relationship. This leaves the Journal writer open to the possibility of something new emerging from an old relationship from the past. That new something may contribute an insight or an awareness which is of benefit to another relationship or which creatively affects the movement in another dimension of life.
                </p>

                <h6>
                  The Dimension of Inner Symbols
                </h6>

                <p>
                  This dimension of life refers to dreams, “twilight imagery” and personal wisdom-figures as the vehicles which come forward spontaneously to carry the movement of life further. The aim of the exercises in this part of the Journal, called the “Depth Dimension,” is to facilitate spontaneous correlations between inner imagery and outer life so that new insights, awarenesses, and possibilities for action and decision-making might come to the surface of consciousness. Then, by means of appropriate Journal exercises, they can be fed back into the ongoing movement of life and thus stimulate growth by creating new configurations in the way things fit together in life.
                </p>

                <p>
                  Progoff tends to shy away from the use of dreams in his method because many people seem unable to deal with them except analytically. The <em>Intensive Journal</em> method of working with dreams is basically to allow the movement in a recurring dream or in a cluster of dreams to suggest some correlation with movement in one of the other dimensions of life. Then the exercitant may use appropriate Journal exercises to work in that dimension of life.
                </p>

              <h6>
                The Fourth Dimension: The Spiritual
              </h6>

              <p>
                As mentioned above, Progoff sees the <em>Intensive Journal</em> work in general as “a species of prayer and meditation... in the midst of the actuality of our life experiences.” However, he came to appreciate the role of the spiritual dimension in creativity only after he had developed Journal exercises in the three dimensions of life treated briefly above.
              </p>

              <p>
                The specifically spiritual dimension is reflected in his program as the dimension of meaning. The procedures for working in that dimension are called “Process Meditation.” In the <em>Intensive Journal</em> program, formal work in-this dimension is reserved for those who have already taken part in the “Life Context Workshop,” which deals with the three dimensions of life treated above. As Rahner has said, “A basic and original transcendental experience is really rooted [in] a finite spirit's subjective and free experience of itself.” <sup>5</sup>
              </p>

              <p>
                The “process” of “Process Meditation” refers to “the principle of continuity in the universe” which is found on three levels: the cosmic, the societal, and the personally interior.<sup>6</sup> The <em>Intensive Journal</em> method helps the individual relate to “process” on the interior level.
              </p>

              <p>
                The movement of life in the three dimensions treated above is characteristically movement toward personal wholeness and the integration of the individual with oneself. Progoff calls that movement “core creativity.” “In terms of individual lives,” he writes, “the essence of process in human experience lies in the continuity of its movement toward new integrations, the formation of new holistic units [of life/time].” <sup>7</sup>
              </p>

              <p>
                In the spiritual dimension of life the movement is characterized by relationships which transcend the core creativity of the individual. The roots of such relationships-even the personal relationship of the individual to God-are to be found in the stuff of everyday life, but at a deeper than ordinary level.
              </p>

              <p>
                Rahner speaks of the knowledge of God as “concrete, original, historically constituted, and transcendental.” He further says that such knowledge of God “is inevitably present in the depths of existence in the most ordinary human life.” <sup>8</sup>
              </p>

              <p>
                Progoff interprets “meditation” broadly. In his usage it refers to whatever methods or practices one uses in the effort to reach out toward meaning. “The essence of meditation,” he says, "lies in its intention, in its commitment to work inwardly to reach into the depths beyond the doctrines of our beliefs." <sup>9</sup> Hence, “Process Meditation” refers to a set of exercises which draw on the individual exercitant's intimations or experiences of connectedness to the principle of continuity in the universe.
              </p>

              <p>
                Progoff describes his method of Process Meditation as follows:
              </p>

              <p>
                Our basic procedure is to reenter the process by which our individual spiritual history has been moving toward meaning.... We reenter that process so as to reconnect ourselves with the inner principle of its movement, and especially so that we can take a further step toward the artwork that is our personal sense of meaning. <sup>10</sup>
              </p>

              <h6>
                Conclusion
              </h6>

              <p>
                In a review of The Practice of Process Meditation, William V. Dych, S.J., translator of Rahner into English, compares what Rahner calls “the universal presence of grace and the Spirit” with Progoff s thesis that “there is in every human being an inner source of new light and life that expresses itself whenever the circumstances are right.” Dych views Progoff’s thesis as so supportive of Rahner's position that it would be hard to imagine a more positive affirmation of it. <sup>11</sup>
              </p>




