<h6>
  Assessments of the Program
</h6>
                <p>
      Louis Levine, Industrial Commissioner, New York State, commented that "one reason the program is so unique and a model for the Labor Department is that they worked so hard with the individual to make this more than a job."
                </p>

                <p>
    The management is naturally pleased at the high percentage that completed the program, entered employment, and stayed on the job. Both department supervisors and residents have commented on the change of atmosphere since the trainees joined the staff. They use words such as "enthusiasm," "interest," "kindness," and "a sense of the joy of life." An evidence of this change, and pinpointing its cause, has been repeated requests of the administration by long-term employees that they too have the opportunity to participate not only in the training program but in the public <em>Intensive Journal</em> program as well. Many have done so.
                </p>

                <p>
    An indication of the "quality of work" aspect of the program is seen in the fact that the geriatric residents increasingly made it a point to request that trainees of the Dialogue House program take care of them, rather than the regular employees. This preference became so apparent at the facility that it began to be a point of friction between the two groups before it was resolved by the <em>Intensive Journal</em> Method in workshop discussions.
                </p>

                <p>
                  The trainees themselves give the testimony as to what the program accomplished. Most of them had little formal education, and many had language difficulties. Foreign speaking trainees were encouraged to write and read journal entries in their native tongue, translating for the group if they cared to. Everyone was advised to forget about grammar, and as they entered more fully into the journal work, their inhibitions about writing were dropped, resulting in freer-flowing expression as well as improved grammar. Their comments from interviews made ten months after training began were recorded and are in excerpted transcriptions.
                </p>

