                  <h1><em>Intensive Journal</em> Target Prison Program</h1>
                <em>
                  <p>A Transforming Experience for a Group of 15 Inmates</p>
                </em>

<h6>
  A. Overview:
</h6>
                <p>
            1. Description and Function of the <em>Intensive Journal</em> Target Prison (IJTP) Program
                </p>

                <p>
        In February, 1997, Dialogue House commenced the <em>Intensive Journal</em> Target Prison (IJTP) Program, a pilot program in which a group of prison inmates would learn how to use the <em>Intensive Journal</em> method over an extended period of time. These inmates would attend the three core workshops (Life Context, Depth Contact and Life Integration), followed by ongoing sessions, evaluations and consultations.
                </p>

                <p>
      Through this extensive series of training and ongoing support, inmates would have an excellent opportunity to learn and then use the <em>Intensive Journal</em> method as a tool in their self-transformation and rehabilitation. We wanted to ascertain the degree to which inmates would respond to the program, including how their behavior and attitude would improve.
                </p>

                <p>
        2. Pilot Site: Bedford Hills Correctional Facility in Westchester County, New York
                </p>

                <p>
        The pilot IJTP program was conducted at the Bedford Hills Correctional Facility in Bedford Hills, New York, which is located in Westchester County, about one hour north of New York City. We chose this maximum security facility for women because the superintendent of the prison, Elaine Lord, is very supportive of the <em>Intensive Journal</em> program and has hosted it previously.
                </p>

                <p>
      Annette Covatta, an experienced leader who has conducted numerous workshops at the Bedford Hills facility, agreed to conduct the workshop series. Report by Annette.
                </p>

                <h6>
                  B. The Workshop Group: 15 Dedicated Inmates
                </h6>

                <p>
        Beginning with 28 inmates, the group narrowed to a core nucleus of 15 dedicated inmates who are committed to learning and using the Method.
                </p>

                <p>

              The workshop group was comprised of women from diverse backgrounds. They have been incarcerated primarily due to substance abuse violations, although some have been convicted of murder or theft. The racial and ethnic backgrounds are also varied.
                </p>

                <p>
  The age of the group is primarily from 25-45 with one inmate over 60 years of age. They are mostly literate and are interested in writing about their life experiences. Many found the sustained period of silence to be a very different way of working and to be highly beneficial and refreshing.
                </p>

<h6>
C. The Workshop Series
</h6>
                <p>
            We held ten workshop sessions, each is five hours in length, for a total of 50 hours of instruction. In the first eight sessions, the group completed the first two core workshop modules, Life Context and Depth Contact, which covered each of the exercises in the <em>Intensive Journal</em> workbook.
                </p>

                <p>
      We then held an introduction to the third module, Life Integration. In place of completing this workshop, the leader found a more beneficial way of helping the group to learn the method: she guided the group through specific exercises of the <em>Intensive Journal</em> workbook that they found to be most relevant and more likely to use after the workshop. By reviewing selected exercises, this helped the inmates to grasp how to continue to use the method on their own. This work comprised the last two sessions.
                </p>

                <p>
      As discussed below, the leader also consulted with the prison officials regarding the progress of the inmates. Evaluation sessions were held with the inmates in which they provided feedback about how they were using the method and its benefits for their self-development
                </p>

                <h6>
D. Evaluation: Officials and Inmates
</h6>

                <p>
1. Support from Correctional Officials
                </p>

                <p>
      Superintendent Lord values the <em>Intensive Journal</em> program a great deal and is committed to offering the IJTP program to other inmates. According to Superintendent Lord, our program has fostered self-growth in individual inmates. The program has created a ripple effect as these inmates have become empowered to help improve the behavior of their fellow inmates.
                </p>

                <p>
                  Ms. Terri McNair, the Family Violence Coordinator and assistant to Superintendent Lord observed the changed behavior in the inmates:
                </p>

                <p>
                  I have been the recipient of the changed behaviors...the workshops have been a tool of incalculable value...some were able to share their writings with others...I have seen women who present a very gruff and rough exterior to the world show a very different side at sometimes great personal risk.
                </p>

                <p>
                  Letter from Terri McNair
                </p>

                <p>
                  2. Inmate Evaluations and Comments
                </p>

                <p>
                  The <em>Intensive Journal</em> program has been a transforming experience for these inmates. Inmates have commented that the <em>Intensive Journal</em> method has helped their ongoing growth and rehabilitation process in many different ways:
                </p>

                <p>
                  <ul>

                    <li>greater self-empowerment</li>
                    <li>release of feelings of anger and frustration </li>
                    <li>increased self-understanding</li>
                    <li>heightened sense of inner peace </li>
                    <li>more self-control (such as in coping with emotions and dealing with peers) </li>
                    <li>additional interest to writing </li>
                    <li>greater awareness of creative abilities </li>

                  </ul>
                </p>

                <p>
                  The <em>Intensive Journal</em> method has become an important part of their lives; they showed initiative in continuing to learn and use the method between sessions. Participants voluntarily requested access to use the books and cassette materials on the method that were provided.
                </p>

                <p>
                  The inmates valued the block of time to work in their lives in a peaceful atmosphere away from the noise and tension of the prison environment. They became more expressive in their thoughts and feelings which led to greater self-awareness and self-control. In her poem, an inmate shows an awareness regarding the importance of listening to her surrounding environment and inner being.
                </p>

                <p>

Poem by inmate participant
                </p>

                <h6>

E. Funding: Foundations and Individual Contributions
                </h6>

                <p>
                  We were able to initiate this program due in large part to the generous contributions from workshop participants who know the value of the <em>Intensive Journal</em> method. To date, we have received $6,010 in contributions from 70 individuals. In addition, we have received funding from the St. Joseph Foundation which is committed to funding innovate programs in the prison.
                </p>

<p>
  However, funding sources for prisoner rehabilitation programs are extremely limited, particularly in light of budget cutbacks and current societal attitudes toward incarceration and punishment. Dialogue House has invested heavily in grant writing efforts to obtain funding from foundations and will seek other sources of funding in the future.
</p>
                <h6>

F. Future Plans and Opportunities in Corrections
                </h6>

                <p>
                The pilot program at Bedford Hills showed the power and effectiveness of offering a continuous series of workshops with a targeted group of participants so that they learn the method and use it over the longer term. The program produced visible results in terms of changed behavior, as documented by the feedback from the prison officials.
                </p>

<p>
  The inmate group desired to continue attending workshops and other inmates wanted to attend. Both the prison superintendent and project manager strongly supported continuing the program. However, due to a lack of funding, the program had to be suspended until this issue could be adequately addressed.
</p>

<p>
  The IJTP program has wide potential in many different areas of corrections from probation, parolees and inmates at various levels of security. The program has been very well received by both inmates and correctional officials and a core base of leaders are very dedicated to conducting the sessions.
</p>

