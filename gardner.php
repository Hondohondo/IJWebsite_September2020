                  <h1>A Personal Journey to Recovery</h1>

                  <p>By Levi Gardner, MA, CSAC</p>

                <p>
                   I once heard a journal instructor describe the <em>Intensive Journal</em> method as an "instrument of integration." He was describing how the human processes at work in the reflective activities of the journal lead somewhere. It isn't always clear at first just where these processes may be leading; the human psyche is fraught with mystery and surprise, and human development across the lifespan uncovers novel and unexpected visions and experiences even as our lives follow the normative courses of adult life. In ways that I've never heard explained to my satisfaction, the <em>Intensive Journal</em> process helps us put the familiar and the mysterious aspects of life side by side. The two experiences I attribute to this are astonishment and understanding.
                </p>

                <p>
                  In my 25 years of working professionally with substance abusers, I've come to the conclusion that addiction can be likened to a small ferry transporting a person from one side of a river to the other. The ferry, for all its clutter, shakiness and batteredness holds a precious cargo, has an intentional aim, and takes on the elements with courage and direction. Addicts suffer terribly, and the only thing more terrible than their suffering is the pain they inflict on others. They suffer in large part because they cannot make sense of where their lives are going. They suffer because they've lost the capacity to make meaning and establish purpose and direction in their life outside of their suffering and addiction. They hurt others because they do not know how to stop hurting themselves.
                </p>

                <p>
                  The <em>Intensive Journal</em> method doesn't heal addiction and its suffering. It doesn't make pain go away. It does something far more important. It can take human suffering, and it can certainly take addiction and its consequences, and clarify them in our moments of truthful conscious presence. Its processes introduce meaning, however lost, and awareness, however blurred, and truthful connection to our emotional and spiritual core. By doing so in silent rooms of reflecting individuals sharing the silence of the Journal process, the method opens inner spaces and connections that engage a gradual awakening of awareness to what is really going on in us. And by in us, I mean in our memories, in our feelings, in our imaginative longings, and in our search for solace and anchor in a passing and uncertain world.
                </p>

                <p>
                      I've been journaling for 27 years; I've been sober for nearly 33. Many personal inventories are included in the journal entries throughout this time. Many dreams and meditative images that have flowed through my awareness during journal practices have moved me through life with a bit better skill and more of a sober and mature focus on the challenges of living. I could go on for quite some time about the personal benefits of the method. But suffice it to say that The <em>Intensive Journal</em> method has been instrumental in the integration of a way of living that works to keep me sober and to deepen experiences of conscious contact with the spiritual, emotional and interpersonal challenges, resources and presences necessary to the rewards and fulfillment of sobriety and inner growth.
                </p>


