                  <h1>Overcoming My Addiction</h1>
                  <!-- <p class="right-span">By Vic T.</p> -->
                  <p>by Patricia Burns</p>

                <p>
              I have been going to Progoff <em>Intensive Journal</em>® Workshops since 1997. At my first workshop, I realized and admitted to myself my addiction to alcohol. The <em>Intensive Journal</em> program, which helped me recognize my alcoholism, is a tool I continue to use in working through the steps of my Alcoholics Anonymous ("AA") program.
                </p>

                <p>
                I started drinking as a teenager, and although I had problems from the beginning--I drank more than my friends, I drank until I passed out, or got sick--I considered my drinking within the norm of teenage behavior. I loved the initial feeling alcohol gave me because it helped me relax and feel comfortable with other people. Usually quiet and shy, I became talkative and fearless after just a few drinks. People were amazed at the transformation; I was the life of the party.
                </p>

                <p>
                My father was an alcoholic, and I equated alcoholism to drinking everyday like he did. My drinking was normal; I was just having fun with my friends. Sometimes while drunk I would be argumentative and do and say things that I regretted the next day. However, because I could stop for long periods of time, I rationalized that I couldn't possibly be an alcoholic. From the time I was sixteen years old until I got married at twenty-seven, I had periodic bouts of heavy drinking followed by strict abstinence. Besides, I had plenty of reasons to drink and always blamed my circumstances for my drinking because during that time period both my parents died. I was twenty-one when my mother died within a year of finding out she had uterine cancer. Three years later, my father died of a heart attack after being ill from a series of strokes, which left him paralyzed on one side.
                </p>

                <p>
            My problems with drinking continued after I got married. After once again getting drunk at a function my husband was catering and starting a fight with him in front of two hundred people, I stopped drinking. Soon after quitting, I became pregnant, and I was able to stay sober for almost five years primarily because of my baby daughter.
                </p>

                <p>
              When my daughter went to school and I went back to work, I decided that I could now be a social drinker. I wanted to be able to have cocktails after work and wine with dinner like everyone else. I had successfully separated myself from my former life. I lived in a different city away from home and the people with whom I grew up. I had no family members living nearby, so I became a part of my husband's family who knew nothing about my drinking. I shut out all memories of my teenage years and young adulthood; it had been an extremely difficult time, and I did not want to relive it.
                </p>

                <p>
    I had been abstinent for five years, but within a few months of beginning to drink again, I had a blackout. I then realized that nothing had changed in my drinking behavior, and I experienced the same problems of being unable to stop once I had the first drink. I went through a period of trying to control it: only drinking wine, only drinking on weekends, or sipping on a glass of water to slow down my drinking.
                </p>

                <p>
              My drinking progressed over the next seven years until eventually I was drinking almost everyday. I did not go out drinking at bars; instead, I was quietly getting drunk at home. My husband worked nights and my daughter was in bed by 9:00 pm, so I spent my evenings alone numbing myself with the alcohol. It calmed my anxiety and mellowed me so I did not have to think or feel. Even though I was unhappy, I did not see that alcohol was the problem. I considered alcohol one of my only comforts.
                </p>

                <p>
              I was always searching for something to help me with my feelings of emptiness, unworthiness, and depression. I went to a couple of different therapists without much success. I became a collector of self-help books. I realized I needed to cut down on my drinking, but I was not acknowledging that I was an alcoholic. I then came across the book At a Journal Workshop by Ira Progoff. I was drawn to it because it focused on therapy through writing. I had used journals for years, and I liked the idea of using writing exercises instead of talk therapy. Working with the book on my own was a frustrating experience, so I signed up for an <em>Intensive Journal</em> workshop.
                </p>

                <p>
During my first workshop, I acknowledged for the first time that I was an alcoholic. My drinking behavior kept coming up in different parts of the <em>Intensive Journal</em> workbook, and I realized that alcohol had dominated my life, first with my father's alcoholism, and then with my own drinking problem, which I had been struggling to control since I was a teenager. Although at times accessing some of these memories was painful, I felt I had made a breakthrough in my problems. It was a feeling of finally having some insight into myself and behavior instead of just experiencing confusion and depression. I felt like I was finally making progress after years of being unable to move forward in my life. It was a powerful feeling.
                </p>

                <p>
      I know to nonalcoholics reading this essay about my drinking that it might seem obvious I was alcoholic, and incredible that I could not see it. It is illogical to deny that I had the illness, given all of the problems I had, but I was a functioning alcoholic. I went to work most every day and I never had a car accident or D. U. I. conviction. I should have or could have had some of those things happen to me especially during my early drinking years when I was out drinking at bars. Because I did not suffer from the more devastating consequences of alcoholism, I could have been in denial much longer. I believe the <em>Intensive Journal</em> method helped me to see clearly my destructive pattern of drinking that seems so obvious now.
                </p>

                <p>
        One of the uses that alcohol provided for me was to shut out the unpleasant memories of the past. I did not want to go back to that time when my parents died and deal with the unfinished business of my relationships with them. Using the <em>Intensive Journal</em> method was nothing like the journal writing I had been doing for years. The structure of the <em>Intensive Journal</em> exercises helped me to go deeper into my life and my history than I was able to do on my own. The writing exercises bring clarity to my life by directing my focus from where I am now to different periods in my past and the circumstances and relationships that were.
                </p>



