                  <h1>Grieving for My Sisters</h1>
                  <!-- <p class="right-span">By Vic T.</p> -->
                  <p>by Christine Lang</p>

                <p>
            In the last seven years I have lost my sister Janice, my sister Norma, and just recently my 86-year-old mother. Now, there is just my beloved father and me. When he dies, I will have lost my entire immediate family, the common memory of our lives together. The grief of these losses has moved through me as the ocean itself, sometimes with waves so turbulent and with such force that I did not think I would be sustained. Other times, gentle tears fell washing ashore in forgotten moments brought to mind through a song or a photo, a piece of jewelry or a holiday.
                </p>

                <p>
                  Yet I was sustained. The power and process of the Progoff <em>Intensive Journal</em> Method and the writing retreats brought a sanctuary for my heart and soul. On the pristine white pages and through the rhythmic flow of the <em>Intensive Journal</em> process itself I have poured out my sorrow and confusion and anger and questions. I have gained entry to locked rooms of feelings and unfinished conversations, been able to rest in the emptiness that can’t be filled except by acceptance and mourning. The <em>Intensive Journal</em> Method has been a friend, a refuge, a treasure, and a lover offering a compassionate, non-judgmental ear to hear all the voices needing to speak.
                </p>

                <p>
          When grief took up residence in my life, each experience was as different as the relationships and the circumstances of each death brought its own tidal wave of responses. When Janice died, it was tangled up in witnessing unimaginable suffering so much so that I used to steel myself everyday just to enter the hospital. I felt overwhelmed by my helplessness and sense of powerlessness to ease her pain. As I write this I can still remember the agony in her voice and her desperation to have life end. Pulled apart by those feelings of wanting it to end for her and railing against her dying at 47 I muddled through that week as best I could. I took solace in knowing I had been there with her. Yet grief demands more than a week, after all the relationship had lasted a lifetime. We had shared a room growing up and all of the secrets and stories that came to inhabit that space between us.
                </p>

                <p>
                It was at the <em>Intensive Journal</em> Workshops that I found a new room, one filled with the sacred space and community of the facilitator and the other participants. All of us gathered together in the silence, in the silence that welcomed me gently and gracefully. In the days that took me through the workshops of Life Context, Depth Contact, and Life Integration, I began to experience the blessings of the process, the magic of how each section can reveal mysteries buried in my own heart and mind while at the same time drawing me ever inward to greater depths and understanding. It was at one of these workshops that I had a dialogue with the event of Janice’s death. I asked it directly why it was so merciless, why now, why this way. The power of that conversation, the opportunity to be honest and heard, was healing. At one point in the dialogue, Death surprised me with its keen awareness and compassion saying, "I’m sorry you had to witness her suffering so much—it was the price you paid for loving her so much and not abandoning her in the end. I know it took everything you had to walk in each day." I did not expect this and I felt my body and heart shift to a different place, one of recognition that Janice’s death was not my personal enemy. Janice’s death came as a result of a complicated set of circumstances and personal choices and medical decisions over which I simply had no control. But I did gain the clarity and affirmation that I took the journey with her as best I could, as far as I could go.

                </p>

                <p>
        At another retreat I began to surrender the torrent of complicated feelings surrounding the death of my sister Norma who was murdered on September 11. Even now there is a surreal quality about writing those words. She literally vanished from life, her body disappearing into the rubble of the Pentagon. The plane that was to take her to the Far East to see my niece and brother-in-law, instead took her to her death. So this time I wasn’t there, wasn’t agonizing over the suffering and what it would be like afterwards. This time it came out of the blue, a mind boggling, heretofore-unknown reality. There was no preparation, no warning, no getting ready.
                </p>

                <p>
                This time there was also unfinished business, unspoken questions, lingering wounds and time was up, just like that. This was just after a summer at the beach together that held out the promise of a new relationship and a future of possibility. The <em>Intensive Journal</em> process invited it all in without censorship or criticism: a space to look at it, feel it, move with it, sometimes with grace and sometimes awkwardly, sometimes with hope and sometimes with despair. There was the community of people who bore it all with me by being in the silence, writing their own stories. Perhaps it is hard to imagine this intimacy in so much quiet, yet it is the very silence that permits us each to hear our own voices, voices able to speak in a space that honors the honesty of whatever must be heard.
                </p>

                <p>
          In the aftermath of both these deaths I used the <em>Intensive Journal</em> Method again to gain some perspective while writing in the Period Log:
                </p>

                <blockquote>
                  It is a time when my family has shifted like the plates of the earth, feelings of eruption and dislocation, tears in the fabric of the family system and the upheaval of roles and rules, communications and connections. It is a time of facing the accelerated mental and physical deterioration of my mother softened by the reconciliation of my relationship with my father and the advancement of an understanding that I never thought possible. It is a time of noticing the aging process and not really liking it or wanting to give it a rousing welcome, a kind of "not now for God’s sake, I’m not in the mood". It is a time of faithfulness and the commitment to give voice to my life in all its disparate elements.
                </blockquote>

                <p>
            Unlike any other relationship, the <em>Intensive Journal</em> Method is always available, always ready to listen, always receiving and giving. The very structure and dynamic of its parts leads me somehow to gain a sense of greater wholeness. Not in one moment or two, but over time the journal, along with the retreats, has given my grief a home, a hideout, and hospitality. By honoring its presence in me, I am being transformed and able to wonder and imagine what might now emerge. To pick up the pieces is never easy or simple. It never goes back to the way it was; the loss is forever. But to walk this landscape with the support of the <em>Intensive Journal</em> process and other companions has offered immeasurable relief and hope.
                </p>


