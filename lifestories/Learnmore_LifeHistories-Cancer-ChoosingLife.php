                  <h1>Choosing Life Over Death
in the Throes of Pancreatic Cancer</h1>
                  <!-- <p class="right-span">By Vic T.</p> -->
                  <p>By Gayle Madden</p>

                <p>
                “You have 3-4 months to live,” said the physician, “one year at the most."
                </p>

                <p>
                  I steeled myself for what lay ahead while not knowing what to expect. The doctor at California Pacific Medical Center in San Francisco explained that my advanced pancreatic cancer was currently inoperable, even after months of aggressive chemotherapy and daily radiation treatments. My husband, Michael, cried while I touched his arm and told him he had been the strong one up to this point, that now it was my turn. I thanked the compassionate surgeon as Michael and I numbly left the office.
                </p>

                <p>
                In February, 2011, three months earlier, I had been diagnosed with the deadly cancer. At first the verdict had thrown me into bouts of depression. It was as if a black cloud of despair would randomly descend, immersing me in hopelessness. During those times I would succumb to what felt like a foreboding physical entity with a life its own, and I would break down sobbing until the darkness cleared. Two evenings after the diagnosis, I felt the dark cloud descending again. I was preparing to sob when I stopped myself.
                </p>

                <p>
                Thanks to the many years I had engaged in Dr. Ira Progoff’s method of <em>Intensive Journal</em>® writing (since 1989), I had learned how to probe deeper and deeper into the well of myself. This was one of the most critical moments of my life, and thanks to Dr. Progoff, I knew what needed to be done. I asked myself, “What am I made of?” and “How am I going to handle this?” Then I waited in silence until the answer revealed itself. A knowing from my core presented itself. It revealed that I had what it takes! I decided in that instant that I was going to live until I die, not die until I die. The heavy veil disappeared, never to return again. I have been sad and have cried since, but the darkness has never returned.
                </p>

                <p>
              On August 2, 2011, four months after the pronouncement of my imminent death, I did make it to surgery. The odds of living through this cancer after surgery were only 11%, but Dr. Assad A. Hassoun, the skillful surgeon who would be performing the Whipple Procedure, said the least I could expect would be to live longer and be able to return to some of my normal activities. The surgery went well. The recovery didn’t.
                </p>

                <p>
                For two weeks I knew I was dying. The doctors knew it too, as they would not say yes when asked if I was going to live. That’s when another critical moment happened; one in which Dr. Progoff’s methods of listening for and receiving inner guidance had prepared me. As a seasoned hiker and backpacker, having recently trekked through the Swiss Alps and the Trinity Alps in Northern California, I tried to walk a little each day with support from my husband. That particular day, I shuffled along the hall in absolute misery. It was all I could do to pick up one foot and put it down in front of the other and I knew that I had to get back to bed. Suddenly, it was as if there was a voice whispering to me, the same voice that comes to me through the <em>Intensive Journal</em> method. I knew in that moment that I had to make the decision right then if I wanted to live or die. I knew that choosing to live could mean living the rest of my life in the pain and misery I was experiencing. It would be easy to say, “I want to die.”
                </p>

                <p>
                  I even considered it. But instead, Dr. Progoff had taught me through his process that there is a pattern to my life, a life unfolding, expressing itself; a life to be lived, appreciated and observed. So based on the strength I had found in my inner self, and the mysteries of existence that I had grown to love, I chose life.
                </p>

                <p>
                The next morning I asked the usual question to my doctor, “Do you think I will live?” For the first time he smiled and said, “Yes, I think you will.” I thought so too!
                </p>

                <p>
              Since that surgery and two-month hospital stay, I have followed the advice of the inner voice that I have come to trust. For years it had been telling me to write. Now I had a story to tell, so I began. I write posts on my blog site called "The Sweet Life: La Dolce Vita @ www.gaylemadden.com" in which I share the sweetness of life. Readers have written emails saying such things as, “Before I read your blog, I was afraid to leave my house. Now I go out and live my life.” I have also written a book titled: An Imperfect Life: A Memoir of Life, Love and Laughter—but Mostly Love. In it I chronicle many of the insights I have gained through using the <em>Intensive Journal</em> method. Some of these understandings have been life-changing, and I feel the ones I have shared here have been life-giving. I credit Dr. Progoff with decades of higher quality life and with receiving wisdom beyond my own understanding.
                </p>

                <p>
              Now that my pancreatic cancer has recurred, and is considered terminal, I am relying on my <em>Intensive Journal</em> writing to give comfort and insights as to why such events have risen in this life. Dr. Progoff has given me the skills necessary to believe in miracles and to recognize them as they occur. Who knows how I would be handling this situation if I had not formed the connection with myself and with that which is greater than myself to guide me.
                </p>

                <p>
              I thank Dr. Progoff for devoting his life to the creation of an instrument that allows me to reach my unconscious, which breathes newer, fresher life into me as surely as soft breezes caress my skin and enliven my soul. And I thank him for reaching others through my writing.
                </p>

                <p>
                  Dr. Progoff’s <em>Intensive Journal</em> method is for Everyman; especially those who are experiencing trauma and are seeking the timeless wisdom that lays forgotten deep within our depths.
                </p>


