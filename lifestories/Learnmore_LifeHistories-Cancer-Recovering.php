                  <h1>Recovering From Breast Cancer</h1>
                  <!-- <p class="right-span">By Vic T.</p> -->
                  <p>By Fran P.</p>

                <p>
                  No matter how old or young, how much knowledge you possess or how centered you feel, when a doctor solemnly sits you down in his office after a biopsy and declares that you have breast cancer, you're stunned. Breast cancer strikes one in eight women in the United States and in 1989, when I was diagnosed, the statistics for survival were not as high as they are now. After wallowing for a few weeks in a trough of despair, I next went into "take charge" mode, which is how I've always dealt with problems. I got other opinions and selected a surgeon and medical team to begin the treatment process. After surgery and during radiation, I joined the Wellness Community where writing was one of several techniques the therapists and social workers offered, but it was the <em>Intensive Journal</em> program that brought perspective and continuity to what I was going through.
                </p>

                <p>
                  Thank God that I had learned and used the <em>Intensive Journal</em> method since 1976. Along with the support of family and friends, psychotherapy, and standard and alternative medicine, the method helped me deal with the end of a long term marriage, and it would help me deal with cancer the same way.
                </p>

                <p>
                  I had been writing in diaries and journals since I learned to write. Then, in my thirties, I began to see that my entries were going in circles--I was venting, recording my life, but not really getting at the heart and soul of myself. When I first learned the beautifully structured ProgoffTM method, the psychological and spiritual soundness of his exercises hit home. I was suddenly able to step out of myself, take a look, and then re-enter my life less fragmented than before after "seeing things differently." All my earlier writing was mostly on the surface and this method helped me to explore parts of my life that I had not looked at, not even in therapy. Dr. Progoff's method also gave me the tools to "spark my Spirit," a term he used,
                </p>

                <p>
                Sometimes, writing in a regular journal can be like writing a laundry list, but Dr. Progoff developed his method into sections and subsections that helped me to explore my life in new ways on paper, and the method became a powerful adjunct to my weekly psychotherapy. The method is based in depth psychology and designed to ground you in the present while mining the past and looking into the future. The structure of the method allows us to safely explore and integrate all the pieces of ourselves. I am a person who needs structure in my life, and it is the structure of this method in which I ultimately found my freedom and my writing began to have a positive effect.
                </p>

                <p>
                In my workbook on the day of the diagnosis, I wrote, "Well, I'll be damned. I'm going to die at 50." Newly divorced and in a relationship again, in my happiness I had managed to keep thoughts of illness and death at bay. I wrote as I made the rounds of doctors, testing, mammograms, bone scans and blood work. I remember laying on the lawn outside of the medical center while waiting for a bone scan as eager, healthy students strolled by laughing. I scrawled right in the middle of writing a Dialogue With Body, "It's not fair. I still have so much work to do, so many people to meet, so many ways to make a difference." As I continued using the method, it gave me the comfort and safety of exploring my fear, anger and despair. Cancer patients and those around them often want to believe that everything is okay. The <em>Intensive Journal</em> workbook was a place where I was allowed to feel the feelings of despair. I was able to let go instead of trying to control everything out of fear, and as a result felt less victimized. In fits and starts, I began to process the fear, pain and seesawing emotions that threatened to paralyze me.
                </p>

                <p>
                  After surgery--a partial mastectomy and removal of the lymph glands (no positive nodes, thankfully)--I was in the hospital for four days. After three days at home, I returned to the hospital delirious, where massive doses of antibiotics were pumped into me to treat a surgically-related staph infection. I was too sick to write and I don't remember much until after that period. Because I have come to think in terms of the <em>Intensive Journal</em> exercises, the method had become part of the family that I call me, and made me feel whole and safe.
                </p>

                <p>
                  Two weeks later I returned home and my friend picked me up and drove me to the ocean nearby to where I live. I often go down to the ocean to write, and it was there that I began to feel that maybe I wouldn't die after all. One particular section of the workbook in which I wrote that day that has always helped me was the Inner Wisdom Dialogue. My inner wisdom figure came to me during my early work with the method, and has continued to comfort me and encourage me throughout the years in our dialogues because I feel that I can discuss anything with her. During the time of my recuperation, I found my dialogues with her to be most useful in not being overwhelmed by the whole experience.
                </p>

                <p>
                Cancer recurred in the opposite breast in 1997, only this time I felt light years beyond my emotional and spiritual level of the original cancer in 1989, due to my continued use of the <em>Intensive Journal</em> program. I again relied on the ProgoffTM method to work myself through the process, along with the support of family and friends. I was now ready to face death in 1997; I was confident I could handle anything because I was in God's hands as were my doctors and caregivers. I also had prior knowledge of cancer, and knowledge is truly power.
                </p>

                <p>
              My <em>Intensive Journal</em> workbook was a good complement to other tools I had been using to help me get through the second treatment more graciously. By 1997, I was also solidly grounded in 12 Step work and my spiritual practices. It wasn't the <em>Intensive Journal</em> program alone that helped me. That's the beauty of the <em>Intensive Journal</em> method. It isn't "the answer" to a whole life, but it's a powerful tool for utilizing all the knowledge and experiences we have as a means for growth and healing.
                </p>

                <p>
                Throughout both bouts of cancer, I found that rather than focusing starkly and despairingly at what could be many months of pain resulting in death, the <em>Intensive Journal</em> method enabled me to look at each aspect of my situation: the treatment, the side effects, the pain and the doctors' and healthcare providers' skills. Both times, I was able to take the different parts of my experience and process them individually instead of getting overwhelmed by the overall, bottom-line fear of the "Big C" (cancer). Within certain areas of the workbook, I gained perspective on the changes taking places in my body by reconstructing the body history and dialoguing with my body during the different stages of treatment.
                </p>

                <p>
                  The workbook also helped me stay in touch with my feelings of fear and anger without imposing them on other people. It is the <em>Intensive Journal</em> exercises that are the only ones that consistently take me deep into myself and bring long-lasting transformation. By 1997, the workbook became my treatment friend, the place I went to talk about everything. It was like a partner in my healing process and I used my partner to go to work every day and to feel less sorry for myself than I would have, had I not been working with the <em>Intensive Journal</em> method.
                </p>

                <p>
                  I have recently been told that I may have to have my left breast removed because it has undergone so much trauma that it's hard to get a crisp mammogram or ultrasound from it. I hope that doesn't happen, but I also know that the <em>Intensive Journal</em> method could help me deal with that, or any other situations that may come in future. As a teacher of writing and journal keeping, I have had countless experiences to write with other people undergoing their own illnesses and setbacks. Dr. Progoff developed his method as a non-analytic process to help each of us recognize our potential and activate it, and it has given me the courage to explore past situations and lay the groundwork for my future. Writing is a powerful healing tool, and the ProgoffTM method is the most powerful of all the methods of journal keeping that I have learned and use.
                </p>


