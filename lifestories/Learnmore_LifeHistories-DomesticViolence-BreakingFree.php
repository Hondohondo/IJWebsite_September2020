                  <h1>Breaking Free From Domestic Violence</h1>
                  <!-- <p class="right-span">By Vic T.</p> -->
                  <p>by Pam Peterson</p>

                <p>
            Looking back on the years I lived lost in the maze of domestic violence, my big question is not why I have come to understand myself much better now, but rather how. Tattered pieces of that answer I sometimes find in the faces of similarly tormented women with whom I work today. Studying their eyes, I can remember the constant fear, the nonstop adrenalin rush, and the false sense of strength I tried so hard to carry into the outside world with me each day.
                </p>

                <p>
                  Eventually, after several years, I found my way out. I cannot emphasize enough my verb choice here _ found. No other word will do. Living under the physical and psychological threat of domestic abuse is like living in a weird and wild amusement park ride that never stops at the gate to let you off. On the good days, in the honeymoon stage, you can feel so great, and then the fear begins again, often escalating to terror. F***ing b***h! You pretend you don't hear it. Stupid c**t! You hope the kids are asleep. Then you get up in the morning, make coffee, and hope he's in a better mood. You wake the kids, try to convince them everything is wonderful as you quake inside, and the crazy ride starts all over again.
                </p>

                <p>
          The ride sometimes ends in a hospital visit, sometimes in an arrest, and more often than we wish to believe, at the morgue. But it can also end in a blessed, if often painful, awareness. A man who abuses a woman has a problem. She has two: him and the fact that she tolerates it. Only when a woman can own her part in a painful and destructive relationship can she find her way out.
                </p>

                <p>
                As I struggled to find my way out of abuse and into a home of my own, the <em>Intensive Journal</em> process became a centering place for me. The exercises guided me one step at a time along the path I needed to travel. I used the <em>Intensive Journal</em> method regularly and also worked for a few months with a trusted therapist. When I was ready to make the transition to a home of my own, I asked for help from a shelter system where my kids and I lived for a week until I could find the place where I would begin my life as a single mom. It was my work in the <em>Intensive Journal</em> program that played a crucial role in helping me to plan the changes. Without the method, I might never have found my truth, or the courage to speak and live my belief that every one of us on this planet has worth, and deserves respect, and has the right to find peace at home.

                </p>

                <p>
        When we regularly work in the <em>Intensive Journal</em> workbook, we move from one section to the next with ease and understanding and let ourselves be taken deeper and deeper to accomplish our soul work. The method helped me to understand the conflicts, themes and patterns I played out over and over again, no matter how much I hurt, or how crazy my life became.
                </p>

                <p>
                At the beginning of a workshop, we identify a period in our lives we are working with or through. At my first workshop, it was easy to look back about two years and see the period of time I would explore, a time of loneliness, depression, and psychological warfare. It began on a winter afternoon in a grocery store aisle, when my husband rammed a grocery cart at my legs. This public display of rage terrified me. I did not at the time say much, which became a deeply entrenched pattern for me. I was voiceless then. At the workshop, as soon as I identified that period of time, a wave of awareness washed through me, and I felt a surge of strength. I realized for the first time in a long while that I had not always felt so sad and afraid and weak. There was another woman inside me, waiting to stand up and fight back.
                </p>

                <p>
          Imagery can be a powerful part of the <em>Intensive Journal</em> process, and for me it was a driving force. The first image I had for this period was of a beautiful, wild stallion lying wounded on a beach, as the waves softly washed around it. I understood, in a deep place inside me, the stallion's pain, and I began to cry. As I cried, the image of the stallion stayed clearly before me. Later in the workshop, I dialogued with it and let it offer advice. Though it may sound foolish to some, my healing began the moment the stallion let me know it would not die there at the water's edge. It would heal and gather strength, and race on. With my many tears that night, I too began to heal and gather strength.
                </p>

                <p>
                The facilitator at the workshop did not intervene as I cried, but rather let me know with her gentle demeanor, eye contact, and invitations to share after certain exercises, that she was there as a witness to my inner work. I strongly believe one of the reasons the <em>Intensive Journal</em> method can be so effective with abuse victims is that there is no fixing, no rescuing, and no fast solution. To break the pattern, the work must be done deep in the bedrock of a woman's soul. Quick fixes can leave victims even more at risk as they yo-yo in and out of relationships that destroy both them and their children.
                </p>

                <p>
            Like any other woman living with verbal violence and threats of physical abuse, I had much inner work to do. My <em>Intensive Journal</em> workbook became the place where I began to see clearly the fear and pain I was living with each day, and where I could ask myself the hard questions of Why? and How did I get here? without worrying about being judged. The workbook was an honest friend, bearing witness to my pain. I could not lie to myself in the work, nor did I want to lie any more, not to myself or anyone else. The more I wrote and worked with the method, the stronger I felt.
                </p>

                <p>
                  The <em>Intensive Journal</em> method helped me realize that my pattern of accepting rough treatment in order to belong somewhere started young. As a child I never felt pretty enough or good enough, so, at sixteen, when I grew into a perfect size seven figure, I began to use it to prove, especially to myself, that I was good enough. Though I was very bright, I stopped using my head or voice about this time, and began swaying my hips to the music, swinging from man to man, in sort of a sexual trapeze act. I meant to be a flower child, a sexy love child, but really I felt small and sad, and just wanted to belong somewhere. That deep aching need led me into an early and rough marriage with an equally needy man who was repeating the pattern he witnessed in his home as a young child. As I began to understand and heal my deep feelings of worthlessness and shame, I began to see clearly I needed to be out of my marriage. But it took a while for me to figure things out. It seemed too big a leap.
                </p>

                <p>
                  Sometime later, in a very powerful exercise, I came to hear a very scared voice inside me, desperately afraid of being poor and being laughed at again, and of making her kids go through the pain of being from a broken home. That part of me was once again filled with shame. By dialoguing in my <em>Intensive Journal</em> workbook, I was able to comfort that scared woman inside me and help her understand the pain was too great, that change needed to happen. During this time, doing this work, I no longer needed to blame my husband so much. I saw he was deeply hurting and in need of help that I could not possibly give him. I began to just take care of myself and stop trying to make him and our marriage better. A couple months later I was living in a place of my own, with so much healing work ahead.
                </p>

                <p>
                  It is many years later now and I feel very blessed. Through my continual work in the <em>Intensive Journal</em> program, I learned how to set boundaries in relationships, how to find my truth, and then speak it and live it. True peace came to live in my home and my heart.
                </p>

                <p>
                  As an educator for 22 years, I know that I have modeled for my students how to live as a strong, compassionate, and autonomous woman, both with a partner and as a single woman. In the last six years, running a program for at risk youth and their families, I have had the chance _ and I cherish it _ to help other women face patterns of victimization and choose to break the chain of violence in their families.
                </p>

                <p>
                  For the rest of my life, I will turn to the <em>Intensive Journal</em> method with all my questions, problems, and forks in the road _ whether under gray skies or blue. The <em>Intensive Journal</em> method is for me a mix of all that is best about therapy and meditation and language of the soul. I encourage any person searching for their truth, and certainly any woman living in fear, to experience the <em>Intensive Journal</em> method.
                </p>


