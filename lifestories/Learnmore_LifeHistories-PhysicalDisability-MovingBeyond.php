                  <h1>Moving Beyond My Handicap</h1>
                  <!-- <p class="right-span">By Vic T.</p> -->
                  <p>By Marie-Blanche Audollent</p>

                <p>
              When I heard about the <em>Intensive Journal</em> program, I had reached a point at which I felt I was merely enduring my life. I was an outside spectator of a life that did not resemble my own, and no longer recognized myself. I felt the need to reconnect with my identity. Deep down, I knew that the true joy of being lies in the creation of one’s own life.
                </p>

                <p>
                At the age of 35, I was not really committed to my profession as an architect, or to my love relationship. Neither was I especially unhappy nor happy, but I was desperately running after the basic joy of life of which I lacked. Moreover, I was struggling with a still too partial acceptance of my handicap—I have been deaf since the age of nine—and I blamed my deafness for many of my problems.
                </p>

                <p>
              The <em>Intensive Journal</em> process gave me the opportunity to take a fresh look at the direction of my life and thus experience myself in the present moment. Working in a section of the workbook called Steppingstones, I became aware of a continuity, or rather a progression, as I wrote down the major events of my life. I gained a greater vision that enabled me to discover a leading thread in the changes and movements of my life. This new way of looking at my existence as a whole opened the door for an integration of my life’s experiences. Naming the contents of my life was already a major first step toward the emergence of my life’s meaning.
                </p>

                <p>
          Another highlight of the <em>Intensive Journal</em> process was the section Intersections, which enabled me to become aware that, having taken certain roads and abandoned others, I have actually been making choices throughout my whole life. To name these choices that were mine and no one else’s meant that I was already taking control of my life and identity. Looking back, I think that these two sections were fundamental in my development, in that they helped me to reposition myself in the movement of my life on the one hand, and to find again my role as primary actor of my existence on the other.
                </p>

                <p>
        The <em>Intensive Journal</em> method turned out to be an integrated system of tools. In the past, I had sometimes written down thoughts in a regular unstructured journal that lacked the adequate framework and different techniques for working on different areas of my life. The <em>Intensive Journal</em> process, however, offers these aspects. In fact, the structure of the <em>Intensive Journal</em> method enables me to not only integrate my thoughts, but also other, deeper aspects of my life, such as dreams and intuitions that would otherwise not have been utilized.
                </p>

                <p>
    I often use the Daily Log section of the <em>Intensive Journal</em> workbook, in which I regularly focus on my everyday life, asking myself questions about the meaning of what I experience and exploring my emotions. This section is much more than a simple diary, as it supports my reflections and helps me to structure my thinking. I also know that at any time I can, if necessary, deepen any aspect of my life in another section.
                </p>

                <p>
              From time to time, I have worked in the Dialogue Dimension, in which one dialogues with the major areas of one’s life. This includes not only people but also important events, one’s body, society and personal Wisdom Figures. The great importance of this work became clear to me immediately. This section often permitted me to move forward and opened up for me thrilling new perspectives. Every time I stepped out of myself, putting myself in someone else’s shoes or identifying with a problem or issue that was on my mind, I was surprised at what I discovered about these important elements of myself and my life.
                </p>

                <p>
        Finally, I began to work on reclaiming my identity and eliminating the aspects that did not belong to me but with which I had identified and had subsequently limited me. I wrote a dialogue with what I called my “interior dragon” and I realized that this “dragon” was in fact a personification of the fears I carried within me and that it controlled me as long as I identified with it. Drawing the dragon out of myself with the help of this dialogue, I was able to understand and then eliminate this identification.
                </p>

                <p>
My work in the <em>Intensive Journal</em> method enabled me to clearly distinguish what comes solely from me (my thoughts, impulses and feelings) from what does not originate from me (certain beliefs and habits). Thus, the dialogues have made me responsible for my thoughts, and as a consequence, for my life.
                </p>

                <p>
      Some of the most beneficial sections in which I addressed the problem of my deafness were the Dialogue with Body and Dialogue with Events, Situations and Circumstances. In these sections, I asked myself questions in the depth of my being about my handicap’s bearing on my life, my past and my identity.
                </p>

                <p>
        Separating myself from my handicap while still being in total intimacy with it in these dialogue exercises, I was able to explore the boundaries between my true identity and the external constraints I faced which demanded certain behaviors. This helped me to stay vigilant and to refrain from making my deafness an excuse for behaviors that were not reflections of my true self and that sometimes harmed me.
                </p>

                <p>
                  For example, in the past, I tended to isolate myself in group situations because of my difficulties with communication. Since I did not explain my problem, other people assumed that I did not want anything to do with them and would avoid me in return. I therefore found myself alone, while I was actually craving personal interactions and relationships. Also, in situations in which it was difficult to understand what was going on, such as conferences, stories, debates and news reports, I would act uninterested, rather than ask for clarification. I thought this defense mechanism was a way of protecting myself, but I was suffering because I was missing out on things in which I was really interested.
                </p>

                <p>
                  Through further work in the <em>Intensive Journal</em> method, I discovered that my handicap, although it makes practical precautions and different arrangements necessary, also reveals the resources of my personality (especially those that help me relate to others). Since it is not possible for me to use one of the most common modes of communication, the telephone, it is not as easy for me to get in touch with others. I have thus learned that I possess the ability to cope on my own in ways such as confronting people face to face. This leads me to communicate directly and in a straightforward fashion, without beating around the bush. Also, in overcoming my communication problems, I have developed my capacity to be a better listener, to be more receptive to others and their needs, and to make others feel understood. I think that being confronted with the boundaries that come with my handicap forced me to develop my abilities to overcome the inherent difficulties. This way, I use all my resources without focusing on the obstacle that my handicap represents, and thus reach my goals.
                </p>

                <p>
                  Also through the dissociation within the Dialogues, I asked myself questions about and familiarized myself with my desires and needs. For instance, I am better able to define which kind of communication I need to establish with others, rather than simply complaining that I have some sensory deprivation.
                </p>

                <p>
                  Sometimes I am drawn by other sections of the <em>Intensive Journal</em> workbook in which I work out of pure enjoyment and in a different way. Such sections include the Entrance MeditationsTM readings and Mantra/Crystals exercises that give me the opportunity to relax and write what comes to me. Within such sections, I obtain greater focus and access my inner being. They also stimulate me to go back to certain basic sections on a deeper level to sum up my experience thus far and for a moment, look back at the past in a structured way before moving forward.
                </p>

                <p>
                  As an instrument of autonomy, the <em>Intensive Journal</em> method allowed me the opportunity to create, little by little, the meaning I now give to my life as I delved into its contents. It has become a companion of my evolving life process. Working with the <em>Intensive Journal</em> process, I have discovered that writing is essential to the experience of my life.
                </p>

                <p>
                  It is true that this work requires an investment of time. But for me, it has become an important and necessary investment. When I need clarity, I open my <em>Intensive Journal</em> workbook, and every time, I close it again with more serenity. My view on my life has changed radically. I am no longer a passive observer of my existence. Moreover, the positive in my life now outweighs the negative. I have gotten used to looking at the positive side of things instead of freezing in the face of the negative. Now, I focus on my good qualities and strengths rather than my faults and weaknesses, and focus on the same in others. I find that this leads me to apply my talents in a more successful way and more often.
                </p>

                <p>
                  I always come out of this work with a clearer vision and a greater understanding of what stimulates, revolts or limits me. The <em>Intensive Journal</em> program has become a very special mode of development and continues to inspire me to evolve.
                </p>

                <p>
                  Having acquired a new awareness of my life, who I am, what I know, what I have done, and also the exact boundary between myself and my handicap, I can now decide which roads I want to take and follow them. As the Italian composer Luigi Nono once said, “No hay caminos, hay que caminar” (There are no ways, there is only the act of walking).
                </p>



