                  <h1>Developing Your Life in Retirement:</h1>
                  <h6>A Conversation with Marge Warren</h6>

                  <!-- <p class="right-span">By Vic T.</p> -->
                  <p>by Amira Pierce</p>

<p>
  <em>
    In the summer of 2001, Amira Pierce, from our staff at Dialogue House, visited with Marge Warren, a long-standing participant in the <em>Intensive Journal</em> program until her death in March 2002. Marge shares with us her thoughts on the method and its impact on her life.
  </em>

</p>


<span>
                    <strong class="qa">
                      Amira Pierce: Initially, what got you interested in the <em>Intensive Journal</em> method?
                    </strong></span>
                    <br>
<p>
  <strong>
    Marge Warren:</strong>
 The truth is, I was attending a class at Marymount [University] and someone came in with a brochure about the Program, which I had never heard of before. She sat next to me and gave it to me but never came back to the class. My first reaction was, I’m not going to go because I don’t like to journal and I don’t like to write, but I kept the brochure, and the more I read it, the more I wanted to try it."</p>


 <span>
                     <strong>
                       AP: &nbsp; What can you tell me about your first workshop experience?
                     </strong></span>
                     <br>
 <p>
   <strong>
     MW:</strong>
  &nbsp; I experienced difficulty in grasping the method at first. But I made a friend there and she reassured me that it gets easier. She was right—I kept going back. What I appreciated most was that we were taught to suspend judgment as we made entries. "Just write what comes; just repeat, don’t analyze," were the instructions.</p>



   <span>
                       <strong>
                         AP: &nbsp; Generally speaking, what about the method has worked for you?
                       </strong></span>
                       <br>
   <p>
     <strong>
       MW:</strong>
    &nbsp; Participation in the Program has helped to change my life. Although I previously had a successful career, I was privately burdened with anxiety, especially when faced with change and when recalling past hurts. In workshops, I was taught to make Journal entries from different perspectives, thus viewing my life from a variety of angles.</p>


 <span>
                     <strong>
                       AP: &nbsp; What sorts of angles?
                     </strong></span>
                     <br>
 <p>
   <strong>
     MW:</strong>
  &nbsp; Writing in the Period Log has given me an interesting view of my life at spaced intervals, thereby enabling me to measure my progress, and writing in the Daily Log, I can appreciate some of the fantastic things that have happened that I might otherwise have forgotten; thus life becomes a wonderful experience. <br class="u-margin-bottom-small-1"> Also, in the Life/Time Dimension section, I review my entire life in a similar way and this further facilitates my awareness of the connection between the various elements of my life that had seemed to be solitary occurrences, I can now appreciate how many pieces fit together in a meaningful manner. It becomes a real miracle.</p>


 <span>
                     <strong>
                       AP: &nbsp; What do you think of the Dialogue Dimension section?
                     </strong></span>
                     <br>
 <p>
   <strong>
     MW:</strong>
  &nbsp; It is just amazing to put a voice to something that is separate from you. It has the power to erase a hurt. The section uses a very special technique—my favorite. It further helps me to see what I usually think of as my own personal problems from different points of view. <br class="u-margin-bottom-small-1"> Dialogue with Events has been most valuable. I have become less traumatized by hurtful happenings and more appreciative of pleasant ones. Even events that had been unpleasant now often bring a glow of happiness when I remember them. And when traumatic events have "spoken with me," they reveal many lessons that have furthered my progress along my spiritual path.<br class="u-margin-bottom-small-1"> Dialogue with Persons takes both the point of view of myself and "the Other"; it has led me to forgive people whom, until I dialogued with them, I had crippling resentments toward and had found it impossible to forgive.
  <br class="u-margin-bottom-small-1"> Dialogue with Works has also been very rewarding. When I could no longer do recreational photography, I was so sad. I felt that this was very unfair. But by dialoging with photography, I was reminded that I had my pictures to show for my years of work and fun. My attention towards these memories changed from grief for what was no longer possible to reveling in the joy I had in taking the pictures. Magically, this feeling was transferred to other memories. I felt as if the camera was really talking to me!  </p>


  <span>
                      <strong>
                        AP: &nbsp; Have you had similar experiences?
                      </strong></span>
                      <br>
  <p>
    <strong>
      MW:</strong>
   &nbsp; As a child, I had been taught to view my body as a temple, but as my limitations increased, I realized that temples fall to ruins, so I was not encouraged until a workshop friend mentioned that "ruins can be restored." That gave me hope. <br class="u-margin-bottom-small-1"> You see, my physical disabilities have interfered with my lifestyle, but through dialoguing with my body, I have become less resentful of these limitations and more aware of the strengths that I have left. After I made one particularly critical comment to my body [in my Journal], it responded with: "I’m doing the best that I can!" That brought me up sharp and I became a little bit more compassionate.</p>

  <span>
                      <strong>
                        AP: &nbsp; What about the section on imagery and dreams section, called Depth Dimension?
                      </strong></span>
                      <br>
  <p>
    <strong>
      MW:</strong>
   &nbsp; I have gained so much from following the techniques taught for use in working with dreams, I have learned things about myself that have helped me to direct some of the behaviors that I wasn’t even aware that I had, behaviors that put obstacles in my path. The Twilight Imagery Log was also helpful in this respect. One particular image, that of a sponge, became rich in symbolism for me.</p>

  <span>
                      <strong>
                        AP: &nbsp; How has the Journal worked for you in your time of retirement?
                      </strong></span>
                      <br>
  <p>
    <strong>
      MW:</strong>
   Retirement can be rather traumatic for people, particularly if a person is enjoying his/her job. Mine was relatively painless, and I’m sure the method helped to make it so. Repeated participation in these workshops has really helped to turn me around. Now I have become aware of inner resources for help. I feel more confident about the future. Growing old is not so frightening, and I have learned that what seemed like wasted time—time of no active production—can be time for the incubation of creative ideas. </p>


  <span>
                      <strong>
                        AP: &nbsp; What would you say are the qualities of a good workshop leader?
                      </strong></span>
                      <br>
  <p>
    <strong>
      MW:</strong>
   The excellent leadership provided in these workshops not only facilitates my understanding of the journal techniques but also encourages me to continue to practice them. The group energy was also a powerful influence. Also, I have established many enduring friendships in the group. The instructions to be given are complex, so the leader must have patience and clarity. The leader needs a special way of saying things. He or she needs a lot of enthusiasm and interest. </p>

  <span>
                      <strong>
                        AP: &nbsp;  Is there anything you would like to say in closing?
                      </strong></span>
                      <br>
  <p>
    <strong>
      MW:</strong>
   Well, mainly that the Progoff <em>Intensive Journal</em> process has enhanced my mind/body/spirit connection, has made me more aware of behaviors that I wish to change, has helped me to enjoy relationships that I did not previously enjoy, and it has helped to clarify some beliefs that I had not realized were creating obstacles in my life. I am so grateful that I have the opportunity to participate in this process.</p>

  <span>
                      <strong>
                        AP: &nbsp; Thank you very much, Marge.
                      </strong></span>
                      <br>
  <p>
    <strong>
      MW:</strong>
 It was my pleasure!</p>

