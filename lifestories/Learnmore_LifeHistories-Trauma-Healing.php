                  <h1>Healing from Sexual Abuse
Using the <em>Intensive Journal</em>® Method</h1>
                  <!-- <p class="right-span">By Vic T.</p> -->
                  <p>by C.S. Rose</p>

                <p>
              As a survivor of years of childhood sexual abuse by my father, the overarching themes of my life have been, survival, shame, self-loathing, anxiety, depression, and loneliness. My first serious suicide attempt occurred when I was in nursing school on my psychiatric rotation. I realized that my history was similar to the patients I was working with and the thought of being in and out of psychiatric hospitals for the rest of my life was intolerable. I felt my life was hopeless. I would never be capable of loving or being loved. I thought, "Nobody cares if you kill yourself. So why don't you put yourself out of this misery?"
                </p>

                <p>
              My attempt was unsuccessful. It resulted in my termination from nursing school. I started years of treatment with psychotherapy and medication. Eventually I was able to work as a nurse's aide in a nursing home. My therapy at this time focused around learning basic social skills, and knowing that one person in this world knew of my hideous past, believed it, listened to my concerns, and cared. I was eventually able to complete my education as an R.N. and work in that capacity.
                </p>

                <p>
              My next serious episode of depression, anxiety, and suicidal thinking occurred after my daughter was born. I thought that because I was married, working and now had a beautiful daughter, somehow my family would see me differently. I thought the abusive dynamics would disappear. The crashing reality that these dynamics would continue and affect my daughter precipitated this next episode. I then had to confront the knowledge that my sexually abusive father was in a position to molest my daughter. I had to confront the issue of sexual abuse again. I was so anxious, angry, and depressed. I was terrified that my husband would leave me. I also had to place my daughter in childcare, because I was unable to care for her and I was abusing alcohol.
                </p>

                <p>
                I planned to kill myself so my husband could have a good wife, and my daughter would then have a good mother. During a prayer questioning God about why I should not carry out my plan for suicide, I had a deep spiritual experience of being loved by God. This experience propelled me back into treatment, with its primary focus on therapy and medication. I then decided to explore other avenues of healing to complement my treatment.
                </p>

                <p>
          It was at this time that I was introduced to an <em>Intensive Journal</em> workshop, a life changing experience for me. The basic question of the method is "where is my life trying to go?" My life history was honored and placed in my <em>Intensive Journal</em> workbook. The overwhelming pain and hopelessness of my life however was not the only focus. It was explained in the <em>Intensive Journal</em> workshop that Dr. Ira Progoff researched the area of creativity. In his research, he found that creative people look at their lives and their work through many different perspectives to solve problems. Dr. Progoff integrated this research into the <em>Intensive Journal</em> process.
                </p>

                <p>
                Participants are led through exercises to write about their life history, work, relationships, health, spirituality, cultural concerns, and dreams. The <em>Intensive Journal</em> method helped me to access and develop these aspects of my life and broadened my awareness of new possibilities for me. I realized that I had defined my life very narrowly. In dealing with my depression I realized that I was in survival mode. It had never occurred to me to see my life as having positive aspects to it, as somehow trying to evolve. As I worked in these different areas of my <em>Intensive Journal</em> workbook, clues about my interests surfaced.
                </p>

                <p>
              In therapy the pain could be oppressive. I felt that I was making so little progress that many times I felt hopeless. The <em>Intensive Journal</em> process invites the participant to write as if you are a "news reporter on your life" without judgment or editing. I had kept journals, but found them depressing because I wrote about my negative judgments on my current lived experience.
                </p>

                <p>
          Working in my <em>Intensive Journal</em> workbook in a safe place, I was able to "dialogue" with my father and mother. The profound anger and sadness over these relationships was captured and made concrete. I was able to work through the very painful process of emotionally and physically separating from them. At the time, I did this for my daughter's safety; only later did the method help me realize the importance of this decision for my own healing.
                </p>

                <p>
The <em>Intensive Journal</em> process helped me identify my strengths. I began to identify the tools I had used to survive. My self-concept changed. My self-esteem gradually increased. Dreams both waking and sleeping are honored and explored. In the environment of privacy and safety, I could write about the "nuts and bolts" of the next small steps I could take to make my dreams a reality. I could also identify some of the barriers that impeded my progress.
                </p>

                <p>
          I discovered that I had tried to bury the first 20 years of my life, as a survival tool. The <em>Intensive Journal</em> process gently and at my own pace helped me to see that "pretending to be normal" was not a helpful attitude. To know myself I would have to look back and acknowledge the abuse of my childhood. With the support of my therapist, I participated in a 12-week incest survivor group, led by a psychologist and a social worker. My <em>Intensive Journal</em> workbook was an invaluable resource at this time, a place where I could express privately and safely, all the multi-layered emotions and insights that group experience provided.
                </p>

                <p>
          One of the most transforming features of the <em>Intensive Journal</em> process for me is the awareness that every word is mine, written in my own handwriting. Oftentimes, it has been hard for me to accept the insights offered by my therapist, family or friends. When in my own writing I come to the same insights, the denials give way for me to own my truth. I cannot deny or rationalize an insight written in my own handwriting--that insight remains written in my <em>Intensive Journal</em> workbook. Weeks, months, or even years later that insight can be reflected on again, and enlarged, leading to deeper insights, healing and growth.
                </p>

                <p>
        I have now worked in my <em>Intensive Journal</em> workbook for the last 15 years. I still find attending the <em>Intensive Journal</em> workshops to be very helpful. Consultants who lead the workshops are professional, skilled and patient in teaching the <em>Intensive Journal</em> process. By taking time away to write about my life, without routine interruptions, and writing in the structure of the workshop, new insights keep surfacing. I keep getting new clues to answer the question, "where is my life trying to go?" I'm healing and growing.
                </p>

                <p>
              I also appreciate the flexibility of the <em>Intensive Journal</em> method. When I was a young mother with many childcare responsibilities, I only had time to attend the workshops which were very helpful and provided further insights for discussion with my therapist. Now, I often use the method daily, but I still find the structure of a yearly <em>Intensive Journal</em> workshop meaningful. The <em>Intensive Journal</em> method is my ally; it is not another obligation. I use it as I need and want.
                </p>

                <p>
                  I am grateful to Dr. Progoff for developing, and Dialogue House for supporting, this holistic approach to address life's central challenges, in a way that promotes healing, growth and integration. The <em>Intensive Journal</em> process is as individual as it is universal.
                </p>


