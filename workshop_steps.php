<p class="u-margin-bottom-small-1">
    Several factors to consider are:
</p>



<p class="u-margin-bottom-small-1">
    <b>  Number of modules.</b> There are 3 core modules (Life Context, Depth Contact and Life Integration) that must be taken in sequence.  You may wish to sign up for one module or the entire series.   Briefly,

</p>
<ul>
    <li> <b>Part 1 - </b> Life Context (LC) covers about half of the exercises and is a very solid experience. </li>
    <li> <b>Part 2 - </b> Depth Contact (DC) covers the remaining half of the exercises. </li>
    <li> <b>Part 3 - </b> Life Integration (LI) helps you develop the material that you started in the first two workshops to see connections and relationships, and to use Dr. Progoff's
        <em>Journal Feedback</em> process. </li>
</ul>




<p class="u-margin-bottom-small-1 u-ma">
    <b>Location.</b>   We list the "location" because you may wish to sign up for a program in your geographic area that has a  local feel to the group, or select a different area that better suits your scheduling needs.
</p>
<p class="u-margin-bottom-small-1">
    <b>Weekend vs. weekday.</b>   We offer programs on both weekends and during the week.  They may be offered weekly or spread a few weeks apart.
</p>
<p class="u-margin-bottom-small-1">
    <b>3-part series. </b>   We offer the 3 core modules during a week-long program that allows you to experience the full method in a continuous way.  There is a day off between modules to compensate for the issue of "Zoom fatigue." We also offer a 3-part series spread out with one module per week.
</p>
<p class="u-margin-bottom-small-1">We urge you to read about the format of the <a class="page-link" href="Intensive_online-series.php">3-Part
        Online Series.</a></p>
<p class="u-margin-bottom-small-1">
    <b>Leader. </b>   You may wish to take the program with a specific leader based upon personal preference or a friend's recommendation.   Please note that all of our leaders are certified and use the same guidelines.
</p>
