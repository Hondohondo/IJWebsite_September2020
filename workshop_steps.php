<h6>How to select an online workshop</h6>

<p class="u-margin-bottom-small-1">
    We are offering online workshops through Zoom that provide you with a range of options to best suit your needs.   Any module that has the letters "ONL" designates that the workshop is being held online. Example: LC-ONL is a Life Context module that is held online. The table of codes for each module is listed and defined <a class="page-link" href="#moduletable">below the workshop calendar</a>.
</p>

